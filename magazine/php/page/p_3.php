<p>Начнем все же со статических веб страниц. Страницу можно редактировать в одном из трех режимов: текстовом, графическом и смешанном. Графический режим предоставляет все необходимое для того, чтобы быстро и качественно создавать веб страницы, производя действия визуально. Не буду говорить о том, как здесь все удобно делается мышкой. Для каждого объекта можно настроить свойства в специальной панели внизу. Без мастеров вставки элементов здесь, ясное дело, не обошлось - они есть и ничем не уступают таковым в других редакторах и даже, можно сказать, превосходят их по удобству. На то он и Dreamweaver.
Волшебная правая кнопка и выпадающие меню открывают нам бОльшие возможности работы с таблицами и свойствами элементов. Это очень важно, ни для кого не секрет, что таблица - основа дизайна в HTML.
Перейдем теперь к коду. Работать с ним ничуть не сложнее, чем с дизайном, на помощь приходит подсветка HTML и автоподстановка тэгов/аттрибутов. При этом окно свойств продолжает работать: выделяем код нужного элемента и редактируем его свойства в меню. Таким образом, разницы для самого редактора между графическим и текстовым режимом нет, последний же дает опытному дизайнеру огромную свободу творчества.
Редактор CSS здесь работает превосходно: он сам предлагает нужные свойства, дает возможность визуального выбора нужного цвета или же изображения.
Неплохо работает подсветка JavaScript, скрипты легко интегрируются с страницами, выходить из Dreamweaver\'а нет смысла.
Скажу честно, пока это лучший инструмент для веб-дизайнера, который я видел. Единственное, Dreamweaver плохо себя чувствует, когда вы редактируете уж слишком сложные структуры из таблиц и стилей, сочетающих абсолютное и относительное позиционирование, а также сложные системы слоев. Такой ужас лучше творить в текстовом режиме.</p>
<p>С недавних пор (а именно, с версии MX) Dreamweaver научился редактировать серверные скрипты. Да, он неплохо редактирует файлы сценариев, подсвечивает и форматирует код. Но не более того. Конечно, этот редактор можно использовать для написания скриптов, но для их тестирования придется воспользоваться услугами браузера и предустановленного вебсервера. Сам редактор предлагает нам протестировать проект уже на сервере хостера.</p>
<p>Теперь расскажу о самом вкусном: фирменных фишечках, фенечках и побрякушечках.
В первую очередь, это менеджер проектов. Если хотите, Dreamweaver возмет на себя всю рутину по загрузке, изменению и обновлению сайта. Ваша задача - продумать структуру и создать сами страницы.
