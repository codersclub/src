<h1>Ручные сканера</h1>
Ручные служат для быстрого и удобного сканирования изображения, недостаток в том что у них очень низкое качество сканирования, и всего лишь единственный плюс их размер, размером они с пульт дистанционного управления телевиза. Сканирование производится путём очень медленного перемещения (вручную) сканера по сканируемому изображению. Никто не рекомендует приобретать такие сканеры, поскольку получаемые с их помощью изображения, как правило, оказываются искажёнными - почти невозможно перемещать сканер по сканируемому объекту с постоянной скоростью.
<h1>Планшетные сканера</h1>
Планшетные сканера - самые распространённые среди пользователей, у них очень высокое разрешение, и средняя скорость сканирования (скорость сканирования сканера зависит от их интерфейса, в данный момент на первом месте скайзёвые сканера, на втором USB, на третьем LPT по быстроте передачи изображения). Наиболее распространённые фирмы сканеров: HP, Canon, Epson, Mustek, Genius, Plustek, Kokuyo и т. д. На данный момент выдам последние планшетные сканера.

