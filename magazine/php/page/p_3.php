<p>Что нам дает такой подход? Тип элементов массива никак не завязан на сигнатуру метода. Также не завязан на сигнатуру и метод доступа - функция CallMethod. Кроме того - такой подход позволяет нам сгенерировать такой массив автоматически, поскольку все, что нам теперь нужно - это создать экземпляр класса MethoThunk с параметром шаблона, соответствующему индексу массива. Попытаемся это сделать. Для этого напишем следующий класс:</p>

<pre class="code">
template<class Base, template<int> class Derived, int NMethods> struct MethodsArray
{
	template<int> class Initer;
	friend class Initer;
	Base* m_Methods[NMethods];

	template<int n> struct ThunkItem
	{
		Derived<n> m_MethodThunk;
		ThunkItem(Base** methods)
		{
			methods[n] = &m_MethodThunk;
		}
	};
	template<int n> struct Initer
	{
		ThunkItem<n> m_Item;
		Initer<n - 1> m_Initer;
		Initer(Base** methods) : m_Item(methods), m_Initer(methods)
		{
		}
	};
	template<> struct Initer<0>
	{
		ThunkItem<0> m_Item;
		Initer(Base** methods) : m_Item(methods)
		{
		}
	};
	Initer<NMethods - 1> m_Initer;
public:
	MethodsArray() : m_Initer(m_Methods)
	{
	}
	Base& operator[](int idx)
	{
		return *m_Methods[idx];
	}
};
</pre>

<p>Как работает этот класс? На вход он принимает 3 параметра:</p>
<p><b>Base</b> - тип базового класса, указатели на который будут помещены в массив;</p>
<p><b>Derived</b> - шаблонный тип производного класса, экземпляры на который будут помещены в массив;</p>
<p><b>NMethods</b> - размер массива.</p>
<p>В классе объявляется две переменных. Первая - <b>m_Methods</b> - массив указателей на базовые класс. Ее, собственно, и будем в итоге индексировать. Вторая - <b>m_Initer</b> - переменная, отвечающая за инициализацию массива m_Methods, и хранит конкретные экземпляры шаблонного класса <b>Derived</b>.</p>
<p>При конструировании экземпляра класса MethodsArray производится конструирование его члена <b>m_Initer</b>. При этом класс m_Initer реализован так, что он рекурсивно включает самого себя (но с разными аргументами шаблона), что позволяет нам сконструировать необходимое количество экземпляров класса Derived. Для того, чтобы рекурсия не была бесконечной -применяется механизм частичной специализации. Если класс Initer инстанцируется с аргументом шаблона, равным нулю, то рекурсивного включения класса не происходит.</p>
<p>Конкретный экземпляр класса Derived хранится в экземпляре класса ThunkItem. Это сделано исключительно для удобства инициализации, дабы не переписывать дважды код, помещяющий в массив нужный элемент. Класс ThunkItem инстанцируется индексом в массиве, по которому нужно разместить экземпляр класса Derived. Этим же числом инстанцируется и класс Derived. Таким образом мы получаем то, что хотели - в нужное места массива помещается экземпляр класса, отвечающего за вызов метода с соответствующим индексом.</p>
<p>В итоге, после отработки конструктора, массив m_Methods содержит необходимое количество указателей на класс Base, что позволяет его индексировать. Но, поскольку фактически там лежат экземпляры класса Derived, инстанцированные соответствующими числами, при вызове его оператора () происходит вызов соответствующего оператора в производном классе, и вызывается нужная нам функция:</p>

<pre class="code">
MethodsArray<MethodThunkBase, MethodThunk, 4> handlers;

handlers[0]();
handlers[1]();
handlers[2]();
handlers[3]();
</pre>

