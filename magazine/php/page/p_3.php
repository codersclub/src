<p>Кроме того, теперь все зависит от реализации класса MethodThunk. В его операторе вызова функции совсем необязательно вызывать какой-либо из заранее реализованных методов - необходимую функциональность можно реализовать непосредственно в нем. Вот решение перовой задачи (про операции), использующее описанный подход:</p>

<pre class="code">
