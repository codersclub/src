<p>Улучшать, вроде бы, и нечего. Но это только на первый взгляд. Сейчас достаточно сказать, что мы можем "попросить" компилятор самому сгенерировать нам массив Methods. Но "попросить" его надо хорошо, так, чтобы он не отказался :).</p>

<p>Для начала мы немного изменим сигнатуры методов. Для этого введем первый вспомогательный тип, устанавливающий однозначное соответствие между целочисленной константой и некоторым уникальным типом:</p>

