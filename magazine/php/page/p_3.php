<p>Казалось бы, эти задачи между собой практически не связаны. Но при пристальном рассмотрении можно увидеть, что в обоих случаях нам нужно сопоставить некоторому числу некоторый метод. Причем однозначно сопоставить. Попытаемся решить эту задачу так, чтобы избежать описанных недостатков.</p>
<p>Для возьмем решение, использующее массив.  Для простоты будем считать, что методов у нас 4: </p>

<pre class="code">
