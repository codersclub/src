<p>На компьютерном рынке сейчас существует практически два крупных производителя процессоров. Если задать любому из вас вопрос "Какой процессор лучше купить?", то за ним последует множество ответов, но, по сути, преобладать будут два варианта: Intel и AMD. Обе эти корпорации не останавливаясь ни на секунду выпускают все более производительные процессоры. При этом идет не простое наращение частоты ядра, а переработка всей архитектуры и блоков процессора. Новый этап эволюции процессоров обе компании видят в направлении увеличения разрядности процессора и использовании на одном кристалле нескольких "независимых" ядер (это уже другая история), которые будут функционировать параллельно. Преимущества микропроцессоров с большей разрядностью очевидны. Они позволяют адресовать больший объем памяти, дают возможность оперировать с большим диапазоном чисел, повышают эффективность параллельных и матричных вычислений и т.д. Начнем рассмотрение с ветерана процессостроения корпорации Intel и его продукта процессора Itanium.</p>

<h1>Intel Itanium</h1>
<p><img border="0" align=left width=120 height=133 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/Itan1.jpg">"Раскрутка" процессора Itanium датируется ноябрем 1997 года. Однако хотелось бы напомнить, что история Itanium началась значительно раньше.</p>
<p>К 1994 г. корпорация Intel испытывала определенные трудности. Продолжавшаяся два года разработка 64-разрядной архитектуры Р7 натолкнулась на серьезные трудности. Впоследствии Intel отказалась от Р7 в пользу EPIC (Explicitly Parallel Instruction Computing - детище Нewlett-Packard, 64-разрядная архитектура). В июне 1994 г. компании Intel и Hewlett-Packard подписали соглашение о совместной разработке новой 64-разрядной архитектуры, ориентированной на применение в серверах и рабочих станциях. Новый процессор получил название Merced в честь реки в Калифорнии (первоначально предполагалось, что системы Merced появятся в 1997 или 1998 г.).</p>
<p>Два года спустя, когда выяснилось, что мощности Merced недостаточно, чтобы при использовании HP-UX (64-разрядная система UNIX) обойти архитектуру PA-RISC, в Нewlett-Рackard решили самостоятельно создавать новый процессор на том же фундаменте, что и Merced, но с иной реализацией внутренних функциональных блоков. Обе компании расширили свое сотрудничество уже над новым 64-разрядным процессором McKinley (так называется высочайшая гора в Северной Америке).</p>
<p>В 1997 г. Intel и Hewlett-Packard представили архитектуру и набор команд IA-64 (Intel Architecture).</p>
<p>В августе 1999 г. впервые появились опытные образцы процессора, а осенью Intel представила Itanium как коммерческое наименование своего первого 64-разрядного процессора.</p>
<p><a href="'. $site['setting']['base'] .'/img/m/0105/cpu64/Itan64.jpg"><img align=right width=180 height=200 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/Itan64.jpg"></a>В октябре 2000 г. появились пилотные образцы систем на основе Itanium.</p>
<p>Первые образцы 64-разрядного процессора Intel представляют собой картридж размером примерно 10х6 см, который включает в себя L3 (кэш-память третьего уровня) 2 либо 4 Мбайт и радиатор. Картридж монтируется в разъем типа Slot и имеет 418 выводов. Процессор имеет трехуровневую иерархию сверхоперативной памяти. Если L1 и L2 интегрирована на кристалле процессора, то микросхемы L3 расположены на самой плате картриджа. На реализацию процессора с соблюдением проектных норм 0,18 мкм потребовалось около 320 млн. транзисторов, из которых только 25 млн. пришлось на реализацию самого ядра, а остальные - на кэш-память. Блок вычислений с плавающей запятой, занимает около 10% площади кристалла. Производительность Itanium составляет до 6,4 млрд. операций с плавающей запятой в секунду. Благодаря архитектуре EPIC и 15 исполнительным устройствам процессор может выполнять до 20 операций одновременно. 266-МГц шина данных позволяет осуществлять быстрые транзакции со скоростью 2,1 ГБит/с, при этом он может непосредственно адресовать до 16 Тбайт памяти. Процессоры Itanium будут работать на тактовой частоте 800 или 733 МГц.</p>
<p>Архитектура Itanium включает такие уникальные средства повышения надежности, как усовершенствованная система обнаружения, исправления и локализации ошибок, предоставляемая архитектурой машинной проверки (MCA), ведение подробного журнала ошибок и код коррекции ошибок (ECC) на кэш-памяти всех уровней и системной шине. Масштабируемость до 512 процессоров.</p>
<p>Для двух- и четырехпроцессорных систем Intel выпустила специальный набор микросхем Intel 460GX, которые могут включаться каскадно, увеличивая число одновременно используемых процессоров. Поскольку конфигурация таких систем изначально предусматривает объемы оперативной памяти в несколько гигабайт, то в системах Itanium применяются сравнительно недорогие микросхемы памяти типа SDRAM. При этом для увеличения производительности, по словам представителей Intel, используются такие методы, как буферирование, чередование и деление памяти на несколько банков. Набор микросхем реально поддерживает работу с 64 Гбайт памяти при максимальной пропускной способности 4,2 Гбайт/с, хотя 64-разрядная адресация памяти теоретически позволяет обращаться к гораздо большему количеству адресов.</p>
<p><a href="'. $site['setting']['base'] .'/img/m/0105/cpu64/Itan64m.jpg"><img align=left border="0" width=180 height=123 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/Itan64m.jpg"></a>В архитектуре Itanium насчитывается по 128 64-разрядных целочисленных регистров общего назначения и 80-разрядных регистров вещественной арифметики, а также 64 одноpазpядных пpедикатных pегистpа. Все они доступны для программирования; кроме того, имеется множество недоступных внутренних служебных регистров, используемых самим процессором. 64 одноразрядных регистра используются для организации логики предсказания ветвления и выполнения команд в порядке, отличном от последовательного.</p>
<p>Современные тенденции развития микропроцессоров связаны с выполнением большего числа команд за один такт. Разработчики IA-64 полагают, что добиваться более высокого уровня суперскалярности (распараллеливания) в процессоре можно, только если отказаться от обычных последовательных кодов и ввести параллелизм прямо на уровень системы команд. В этом случае задача распараллеливания ложится не на аппаратуру процессора, а на компилятор. Как уже отмечалось, в основе IA-64 лежит технология EPIC, главная идея которой - введение явного параллелизма. С выходом Itanium сравнение процессоров по частоте практически теряет смысл. Теперь придется применять новые методики, учитывающие не только количество реально выполненных за один такт инструкций, но и качество анализа компилятором исполняемой программы, поскольку результирующая производительность будет сильно зависеть от этого (процессор ведь может работать с огромной скоростью, вычисляя ненужные ветви программы).</p>
<p>По заявлению разработчиков, Itanium полностью совместим с современными 32-разрядными приложениями. Однако вряд ли эти программы будут работать на 64-разрядном кристалле быстрее. Более того, как полагают некоторые специалисты, возможно, придется привыкать и к более медленным темпам.</p>

<h1>Intel Itanium 2</h1>
<p><img border="0" align=left width=88 height=109 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/itan2.gif" style="margin-left: 0px; margin-top: 0px;">Процессор Intel Itanium 2 (с ядром Madison) с объемом L3 6 МБ оптимизирован для выполнения требовательных корпоративных и научно-технических приложений.</p>
<p>Выпускается в 3-х модификациях:</p><br>
<ol>
<li>с L3 объемом 6 МБ для многопроцессорных и двухпроцессорных серверов и рабочих станций;
<li>с тактовой частотой 1,40 ГГц и L3 объемом 1,5 МБ, оптимизированный для двухпроцессорных серверов и рабочих станций;
<li>с пониженным энергопотреблением, оптимизированный для двухпроцессорных серверов с высокой плотностью монтажа и рабочих станций (максимальная потребляемая мощность - 62 Вт).
</ol>
<p>Инструкции, обрабатываемые шестью параллельными конвейерами глубиной 8 команд, непосредственно выполняются в 23 функциональных блоках.</p>
<p>Исполняющие модули Intel Itanium 2: 6 целочисленных модулей; 2 модуля для вычислений с плавающей точкой; 2 модуля для вычислений с плавающей точкой двойной точности; 6 модулей для обработки мультимедийных команд; 4 модуля, управляющих загрузкой и выгрузкой данных; 3 модуля ветвлений.</p>
<p>Количество соответствующих модулей оптимально подобрано с учетом потребности в вычислительных ресурсах приложений, характерных для корпоративных СУБД, сложных инженерных расчетов и др. При этом команды практически не "задерживаются" в ожидании, пока освободится нужный исполняющий модуль. Ширина системной шины, по которой осуществляется взаимодействие нескольких процессоров друг с другом и доступ к памяти, увеличена до 128 бит, что также кардинально сказывается на скорости работы.</p>
<p>Число регистров, в которых размещаются данные для непосредственной обработки их процессором, свыше трехсот. Большое их количество позволяет избежать дефицита регистров при параллельной обработке многих команд. Кроме того, в процессоре Intel Itanium 2 реализованы такие механизмы повышения эффективности работы, как стек и переименование регистров.</p>
<p>Регистры Intel Itanium 2: 128 регистров общего назначения; 128 регистров с плавающей запятой; 64 регистра предикатов; 8 регистров перехода.</p>
<p>Объем кэш-памяти третьего уровня процессора Intel Itanium 2 достигает 6 Мб (доступ к кэшу в несколько раз быстрее, чем к "медленной" оперативной памяти, и составляет 48 Гб/с). Архитектура Itanium включает такие уникальные средства повышения надежности, как система расширенного самоконтроля EMCA, обеспечивающая обнаружение, коррекцию и протоколирование ошибок, а также поддержку обработки кода ECC - контроля четности.</p>
