<h2>OpenOffice.org и другие альтернативы MS Office</h2>
<p>Лицензионная версия Microsoft Office 2003 Professional стоит $365. Для российского пользователя это достаточно большая цена, и, естественно, бесплатное решение для него - большая удача. И такое решение есть - OpenOffice.org. Этот пакет включает в себя несколько программ не менее удобных, чем у конкурента, а иногда, и более хорошо и качественно сделанных. Для MS Word аналогом является OO Writer, для Excel - OO Calc, для PowerPoint - Impress. Интерфейс OpenOffice.org аналогичен интерфейсу MS Office, поэтому сложности при переходе на этот офисный пакет сведены к минимому. Также OpenOffice.org поддерживает форматы DOC, XLS, PPT, то есть форматы MS Office, однако если при переводе файла  из одного формата в другой, не стоит сетовать на разработчиков OpenOffice.org, ведь форматы MS Office закрыты для общественности, и “открытый офис” не всегда правильно интерпретирует файлы Office. У автора проблемы возникали лишь с позиционированием картинок, но эту ошибку можно легко исправить. Интерфейс OpenOffice.org русифицирован (кстати среди всех офичных пакетов русифицированы лишь MS Office и OpenOffice.org, а остальные в лучшем случае правильно обрабатывают русские тексты и проверяют их спеллером), в последней версии появилась русская проверка орфографии. К тому же открытый офис существует в версиях для Linux, Solaris, BSD. Для корпоративных пользователей на этом же движке существует StarOffice ($60) производства Sun Microsystems.</p>

<h2>Графический редактор.</h2>
