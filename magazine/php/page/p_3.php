<h1>Hammer - процессор 8-го поколения корпорации AMD</h1>
<p><a href="'. $site['setting']['base'] .'/img/m/0105/cpu64/64_3800.jpg"><img border="0" align=left width=115 height=114 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/64_3800.jpg" style="margin-right: 0px;"></a><a href="'. $site['setting']['base'] .'/img/m/0105/cpu64/Athlon64.jpg"><img border="0" align=left width=115 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/Athlon64.jpg" style="margin-left: 3px;"></a>Второй производитель микропроцессоров с архитектурой x86 - корпорация AMD тоже обнародовала свои планы создания 64-разрядных кристаллов. Однако в отличие от конкурентов она избрала эволюционный путь: добавила 32 разряда к уже имеющимся 32. Теперь регистры расширились до 64 разрядов, появились команды манипуляции с 64-разрядными данными, да и шина адреса увеличилась до 64 разрядов. В итоге родилась архитектура x86-64. Команды нового кристалла отличаются от команд процессоров x86 только наличием префикса, указывающего на их разрядность. Обеспечена полная аппаратная поддержка выполнения инструкций x86-32 на уровне ядра. В отличие от процессора Itanium, здесь должна обеспечиваться полноценная реализация 8-, 16- и 32-разрядных приложений без потери производительности. Таким образом, на одном процессоре смогут одновременно и независимо работать 16- и 32-разрядные приложения.</p>
<p>Весь поток процессоров Hammer "разбит" на два русла: Clawhammer (для настольных ПК и ноутбуков, Athlon64) и Sledgehammer (серверные процессоры рассчитанные на работу в конфигурациях от 2х до 8ми процессоров, Opteron). Процессоры Hammer производятся по технологии 0.13 микрон с медными межсоединениями и с использованием технологии SOI с дальнейшим переходом на технологию 0.09 микрон. Ядра процессоров Hammer закрыты металлическими пластинами, которые выполняют еще одну функцию - равномерно распределяют выделяемое тепло.</p>
<p><a href="'. $site['setting']['base'] .'/img/m/0105/cpu64/Athlon64_2.jpg"><img border="0" align=right width=150 height=41 src="'. $site['setting']['base'] .'/img/m/0105/cpu64/Athlon64_2.jpg"></a>Athlon64 использует новый разъём Socket 754, а Opteron - Socket 940. Различие в числе контактов двух процессоров Hammer связано с наличием дополнительного канала памяти и двух портов HyperThransport на Opteron. Все процессоры Hammer будут использовать упаковку mPGA.</p>
<p>Если сравнивать новый процессор от AMD с поколением К7, то тут сразу бросаются в глаза основные отличия:</p>
<ol>
<li>Интегрированный контроллер памяти, двухканальная или одноканальная, как небуферизованная (unbuffered), так и регистровая буферизованная.
<li>16-битная 600/800 МГц DDR (dual simplex) шина HyperTransport для соединения с AGP и южным мостом.
