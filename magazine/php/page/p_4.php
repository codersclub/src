<h2>Поочередная установка ОС</h2>
<p>С одной стороны, все очень просто: устанавливаем ОС, перегружаемся, загружаемся с загрузочного диска (или дискеты), устанавливаем ОС, перегружаемся ...... и так повторяем столько раз, сколько нам нужно операционок. Но эта простота может оказаться очень опасной, одно неверное движение и можно потерять все данные, поэтому:</p>
<p><b>!!! ПРЕЖДЕ ЧЕМ ДЕЛАТЬ ЭТО, СДЕЛАЙТЕ РЕЗЕРВНУЮ КОПИЮ !!!</b></p>
<p>Ну все, уже нормально напугал, теперь перейдем к самому процессу.</p>
<ol>
<li>Разбиваем винт на нужное количество разделов и форматируем</li>
<li>Устанавливаем ОС</li>
<li>Устанавливаем загрузчик</li>
<li>Устанавливаем ОС2 (номер Х)</li>
<li>Обновляем загрузчик</li>
<li>Если надо еще одна ОС, переходим к п.4</li>
<li>Все</li>
</ol>
<p>Теперь более подробно рассмотрим каждый пункт.</p>
<p>Для начала нужно проверить BIOS, если есть защита boot-сектора, нужно отключить.</p>
<p><b>Разбивка диска:</b> желательна (для некоторых систем просто необходима). Для разбивки лучше использовать специальные утилиты (я предпочитаю Partition magic 7 или 8 версию). Разбивать желательно для каждой ОС свой раздел + раздел для данных. Некоторые загрузчики разрешают устанавливать несколько ОС в один раздел (при загрузке они подменяют системные папки), но все же лучше для каждой ОС свой диск. Более надежно, меньше влияние одной ОС на другую и убивать намного легче - форматнул винт и как и не было. Желательно работать с программой стороннего производителя, она видит информацию о многих разделах и показывает какая система установлена на каком разделе.</p>
<p><b>Примечание.</b> Очень распространенная ошибка при использовании встроенных утилит работы с дисками: Если одна ОС не видит файловую систему другой ОС она предложит отформатировать "неразмеченную" область. Другая распространенная ошибка, связана с подменой дисков, допустим у нас есть:</p>
<p>C:\ - NTFS (установлена ХР); D:\ - FAT32 (диск с данными); E:\ - Ext (Linux); F:\ -FAT32 (диск с данными).</p>
<p>И в один прекрасный день вы решили, что ХР вам уже не нужна (или хотите ее переустановить). Вы загружаетесь с загрузочной дискеты, format C:, теперь вы перегружаетесь и очень сильно удивляетесь!!! Загружается ХР!!! А что же я только что форматировал, спросите вы. А форматировали мы диск D:\, т.к. старые утилиты не видят разделов отличных от FAT (NTFS, Ext, ...), когда мы форматировали команда format видела только 2 диска D:\ - FAT32, который стал С:\ и  F:\ -FAT32, который стал D:\.</p>
<p>Нужно быть очень осторожным и внимательным, также желательно использовать программы, которые предназначены для работы с дисками.</p>
<p><b>Установка ОС.</b> все понятно ставим ОС, дрова, ..... (если есть защита boot-сектора, нужно отключить)</p>
<p><b>Установка загрузчика.</b> этот пункт желателен, но не обязателен, каждая современная ОС имеет встроенный загрузчик, LILO для Unix-систем или встроенные в ОС, как в ХР (отключается: Мой компьютер -> Свойства -> Дополнительно -> Загрузка и восстановление). Почему я рекомендую установку именно загрузчика от стороннего производителя: они более функциональные (часто в них встроены утилиты администрирования и обслуживания ПК), имеют средства разделения загрузочных/системных файлов каждой ОС, легко восстанавливается загрузочная область и много других полезных мелочей.</p>
<p><b>Обновление загрузчика.</b> После установки новой ОС, она обычно затирает boot-сектор или устанавливает свой загрузчик, правда действует это не всегда, только если обнаруживается предыдущая версия  ОС, например, если нужно две ОС: 98 + ХР, сначала нужно устанавливать Windows98, а потом WindowsХР (только в этом порядке, если сделать наоборот, то Windows98 затрет загрузку ХР) после установки поставит свой загрузчик (вполне функциональный, как для этих двух ОС). В случае других ОС (я рекомендую и для этого варианта тоже) необходимо обновить загрузчик. Как его обновить зависит от конкретной программы. Некоторые программы надо проинсталлировать еще раз, некоторые после установки (после п.3) предлагают сделать загрузочную дискету, для обновления нужно загрузиться с этой дискеты, есть еще один вариант, в папке загрузчика есть файл, которые обновляет загрузку (для Acronis OS selector, reinstall.com).</p>
<p>При установке ОС нужно быть очень осторожным, внимательно читать, что пишут программы и отвечать на их запросы только после того, как хорошо подумать. Не один десяток гигабайт информации потеряно.
<p>Преимущества и недостатки этого метода после рассмотрения второго метода, т.к. недостатки одно, плавно перетекают в преимущества другого.</p>

