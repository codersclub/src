<h2>Различия этих способов (недостатки и преимущества)</h2>
<p><b>Скорость работы.</b> Конечно же скорость выше при нормальной установке ОС. При использовании виртуальных машин, скорость будет ниже, т.к блоки реальной машины эмулируются - эта нагрузка ложится на центральный процессор, помимо обработки самих задач. Скорость работы машины различна для программ Connectix Virtual PC и VMware Workstation, также на скорость влияет объем выделенной реальной памяти для виртуальной машины.</p>
<p><b>Риск потери данных.</b> При работе с виртуальной машиной потерять данные (с винчестера) практически не возможно. Доступ к обычным разделам осуществляется как к сетевому ресурсу (можно убрать доступ или открыть только для чтения).</p>
<p>Совсем другая ситуация при поочередной установке систем. Здесь данные можно потерять даже до того, как установится вторая ОС (см. примечание к разбивке диска). После установки ОС потеря данных также актуальна, как и до установки. Доступ к дискам может быть прямой (диски Windows 98/Ме видно в ХР), при этом любое программное обеспечение (которое вы, например, тестируете) может запортить информацию. Также ОС может обнаружить "неразмеченную" область и предложить ее отформатировать или в случае использования систем Unix, можно некорректно монтировать диск. Защита информации полностью лежит на пользователе.</p>
<p><b>Недоступность данных.</b> Опять лучше создание виртуальной машины: какие бы ОС не были установлены, можно получить доступ к любым данным, или подключить диск, или сделать как сетевой ресурс. Намного сложнее при поочередной установке ОС, здесь что бы получить доступ нужно пользоваться сторонними утилитами, которые работают не всегда корректно и не на каждый раздел можно подключить. Например, при работе в Windows 9х доступ к разделам NTFS, Ext и другим осуществляются с помощью сторонних утилит (по поводу доступа к NTFS у меня были проблемы с "читаемостью" русских названий файлов, записи на этот диск, и доступ к некоторым файлам).</p>
<p><b>Переход из одной системы в другую.</b> В случае поочередной установки необходима перегрузка системы, работа в нескольких системах одновременно исключена. Для виртуальной машины, переход осуществляется запуском нужной ОС. При этом новая ОС запускается в отдельном окне и сохраняется полная работоспособность текущей ОС. Количество запущенных ОС ограничено скоростью процессора и объемом памяти. Запуск и загрузка системы необязательны, если предварительно виртуальную ОС усыпить (или сохранить) - доступ, в этом случае, мы получаем через несколько секунд.</p>
<p><b>Поддержка оборудования.</b> Виртуальная машина заменяет некоторые реальные компоненты на свои (мышь, видеокарта, ...), поэтому тестирование некоторых элементов становится не возможным. Для поочередной установки под каждую ОС нужно устанавливать свои драйвера, поддержка железа полная, т.к. работает только одна система: при корректной установке конфликтов быть не может.</p>

