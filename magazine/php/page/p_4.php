<h2>Математика</h2>
<p>На любом языке программирования высокого уровня, чтобы сложить два числа нужно сделать что-то вроде этого:</p>
<p>переменная=число1 + число2</p>
<p>В ассемблере все по-другому. Чтобы сложить, вычесть, умножить или разделить два числа он использует не переменные, а регистры процессора. Регистр процессора – это как бы переменные, которые находятся в процессоре. Вот таблица с основными регистрами:</p>
<br>
<div class="info">
EAX	используется для сохранения числа и для счета<br>
ECX	используется для счета<br>
EDX	сохраняет остаток от деления <br>
EBX	может быть использован для всего<br>
ESI	используется для управления строками<br>
EDI	также используется для управления строками<br>
</div>
<p>И таблица с основными операторами, которые используются для счета:</p>
<br>
<div class="info">
MOV EAX,число1	копирует число1 в регистр EAX<br>
ADD EAX,число1	прибавляет к содержимому EAX число1<br>
SUB EAX,число1	отнимает от содержимого EAX число1<br>
MUL регистр1	используется для умножения, умножает содержимое регистр1 на число, которое всегда хранится в EAX и сохраняет результат в EAX<br>
DIV регистр1	Делит содержимое EAX на содержимое регистр1 и сохраняет результат в EAX<br>
</div>
<p>Вообще все арифметические операции в ассемблере производятся между регистром и аккумулятором, то есть регистром EAX. В операторах MUL и DIV только один параметр, потому что ассемблер сразу знает, что второе число хранится в EAX. Но почему же в операторах SUB и ADD два параметра, а не один? Об этом мы поговорим позже после того, как рассмотрим пример в конце статьи.</p>
<p>Теперь немного подробнее об арифметике. В регистре EDX всегда хранится остаток от деления и вы должны запомнить, что перед делением нужно обязательно очистить этот регистр. Это можно сделать так:</p>

<pre class="code">
MOV EDX,0
</pre>

<p>или так:</p>

<pre class="code">
XOR EDX,EDX
</pre>

