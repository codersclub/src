<p>This section contains technical information for programmers who want to create DLLs for use with mIRC.</p>

<p>The routine in the DLL being called must be of the form:</p>

<p>int __stdcall procname(HWND mWnd, HWND aWnd, char *data, char *parms, BOOL show, BOOL nopause)</p>

<p>mWnd is the handle to the main mIRC window.</p>

<p>aWnd is the handle of the window in which the command is being issued, this might not be the currently active window if the command is being called by a remote script.</p>

<p>data is the information that you wish to send to the DLL. On return, the DLL can fill this variable with the command it wants mIRC to perform if any.</p>

<p>parms is filled by the DLL on return with parameters that it wants mIRC to use when performing the command that it returns in the data variable.</p>

<p>Note: The data and parms variables can each hold 900 chars maximum.</p>

<p>show is FALSE if the . prefix was specified to make the command quiet, or TRUE otherwise.</p>

<p>nopause is TRUE if mIRC is in a critical routine and the DLL must not do anything that pauses processing in mIRC, eg. the DLL should not pop up a dialog.</p>

