<h2>Проект</h2>
<p>Для изучения процесса разработки приложений с помощью KDevelop создадим небольшую утилиту просмотра свойств файлов формата WAV. Приложение управляется с помощью меню и отображает основные свойства звукового файла.</p>
<p>Приложение должно: </p>
<ul>
  <li>иметь простой интерфейс;</li>
  <li>позволять последовательно открывать файлы;</li>
  <li>определять формат файла, отсеивать неподходящие файлы;</li>
  <li>отображать свойства файла.</li>
 </ul>

