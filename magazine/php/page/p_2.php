<h2>Сервер управления конференциями(MCU)</h2>
<p>Сервер MCU поддерживают соединения между тремя и более конечными устройствами. По спецификации H.323 сервер MCU состоит из непосредственно контроллёра (MC) и нуля или большего количества процессоров соединений (MP). MC обеспечивает H.245 согласование между всеми терминалами для определения общих характеристик обработки аудио|видео сигналов. MC также управляет ресурсами соединения, определяя, какой аудио и видео поток, если таковой имеется, будет многоточечным.</p>
<p>MC напрямую не взаимодействует ни с какими медиа потоками. Этим занимается MP, который смешивает, переключает и обрабатывает потоки аудио|видео|данных. MC и MP характеристики могут сосуществовать в отдельном компоненте или быть частью какого-нибудь H.323 компонента.</p>
