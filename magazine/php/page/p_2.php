<h2>Главная форма приложения</h2>
<h3>Создаём форму</h3>
<p>Для создания главной формы приложения необходимо воспользоваться утилитой Qt Designer. Для этого в KDevelop с помощью меню File-&gt;New открываем 
диалог создания нового файла. В диалоге на вкладке General выбираем Qt Designer File (*.ui) и в поле Filename вводим mainform.ui. При нажатии на [OK] среда 
предложит открыть файл в ASCII режиме, от этого надо отказаться. В открывшемся Qt Designer\'е выбираем тип создаваемой формы. В нашем варианте это будет Main 
Window.</p>
<p>Уберём все галочки в мастере создания формы. Таким образом мы получаем пустую панель. Если редактора свойств нет на экране, то вызываем его с помощью меню Window-&gt;Views-&gt;Property Editor/Signal Handler. Теперь необходимо заменить в свойствах формы её наименование (name) на MainForm и отображаемый заголовок (caption) на KWaveInfo. </p>
<h3>Добавляем меню</h3>
<p>Наше приложение будет управляться с помощью меню. Для создания меню надо нажать на правую кнопку мыши на форме и выбрать из контекстного меню элемент Add Menu Item. Переименуем появившийся элемент меню в &amp;File. Также создадим элемент &amp;Help. Для создания подэлементов элемента File необходимо воспользоваться так называемыми "действиями" (actions).</p>
<p>Если окна Actions нет на экране, то это исправляется с помощью меню Window-&gt;Views-&gt;Action Editor. В этом окне в контекстном меню выбираем элемент New Action, в окне появляется действие с именем Action. Выделяем его мышью и в редакторе свойств производим следующие действия:</p>
<ul>
  <li>изменяем свойство name на fileExitAction;</li>
  <li>изменяем свойство text на Exit;</li>
  <li>изменияем свойство menuText на E&amp;xit.</li>
 </ul>
<p>Аналогично создаем действия fileOpenAction, fileCloseAction и helpAboutAction.</p>
<h3>Сигналы и слоты </h3>
<p>Термины "сигнал" и "слот" в данном случае означают в терминах программиста под Win32 "событие" и "обработчик" соответственно.</p>
<p>Для создания слота у формы надо вызвать её контекстное меню и 
выбрать элемент Slots..., это приводит к появлению окна редактирования слотов. С 
помощью кнопки [New Slot] создаём новый слот. В свойствах слота меняем newSlot() 
на ExitSlot() и нажимаем на кнопку [OK]. Аналогично создаём слоты OpenSlot(), 
CloseSlot() и AboutSlot().</p>
<p>Теперь надо соединить действие fileExitAction с созданным слотом ExitSlot(). Для этого выделяем действие в окне Actions и нажимаем на синекрасную кнопку расположенную выше списка действий. В открывшемся окне редактирования соединений в левом верхнем углу расположен список сигналов, которые может генерировать объект; в правом верхнем - список слотов главной формы; внизу окна отображаются созданные соединения.</p>
<p>Выделим сигнал activated() и слот ExitSlot(), нажмём на активировавшуюся кнопку [Connect]. В списке созданных соединений появится новая строка.</p>
<p>Делаем аналогичные действия для действий fileOpenAction, fileCloseAction и helpAboutAction.</p>
