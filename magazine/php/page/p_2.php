<h2>Интернационализация приложения</h2>
<h3>Что такое i18n</h3>
<p><FONT size=2>i18n является системой интернационализации, которая 
используется для работы с интернационализированными (ну и словечко :) версиями 
приложений или проектов. Обычно приложения поддерживают только родной язык 
автора, данное обстоятельство немножко напрягает людей не знакомых с языком 
автора. Цель интернационализации представлять приложение на языке пользователя. 
</FONT></p>
<h3>Как KDE поддерживает интернационализацию</h3>
<p><FONT size=2>Одной из задач KDE является предоставление 
приложений пользователю на его родном языке и упростить для разработчика процесс 
перевода своего приложения. </FONT></p>
<p><FONT size=2>Технически, это реализовано в стандарте KDE File 
System, который включает в себя поддержку локализации и предоставляет 
приложениям поддержку интернационализации через использование класса KLocale 
библиотеки KDE-core. </FONT></p>
<p><FONT size=2>Со стороны разработчика надо сделать следующее: 
</FONT></p>
<ul>
  <li><FONT size=2>Подключить kapp.h к исходному коду; </FONT>
  <li><FONT size=2>При работе с отображаемым текстом заключать его в i18n() 
  макрос; </FONT>
  <li><FONT size=2>При использовании локали использовать klocale() макрос. 
  </FONT></LI></ul>
<p><FONT size=2>Это всё, что надо будет помнить при 
программировании. Отметьте, что не следует интернационализировать 
конфигурационные строки, которые используются KConfig. </FONT></p>
