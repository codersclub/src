    {
        case 1: Temp += " / Mono"; break;
        case 2: Temp += " / Stereo"; break;
        case 4: Temp += " / Quadro"; break;
        default: Temp += " / Unknown"; break;
    }
    edChannels-&gt;setText(Temp);
    Temp = Temp.setNum(sRiff.Frequency);
    edFrequency-&gt;setText(Temp + " Hz");
}
