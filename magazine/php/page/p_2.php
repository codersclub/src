<p>К сожалению, ситуация с электропитанием вряд ли улучшится в ближайшем будущем - и это обусловлено вполне объективными причинами. Электростанций не становится больше, а устаревшее промышленное оборудование будет заменено нескоро в силу недостатка средств у предприятий. А значит, нам с этими проблемами жить.</p>
<p>Согласитесь, что утерянные файлы данных и поврежденные компоненты, даже при желании, малорентабельными последствиями отключения энергии не назовешь. Другая проблема, с решением которой приходится сталкиваться в подобной ситуации, состоит в том, что после того как питание будет восстановлено, сеть необходимо привести в прежнее состояние: неожиданное включение питания может сопровождаться скачками напряжения. В качестве меры предосторожности администраторам сетей следует проследить за тем, чтобы все персональные компьютеры, нагреватели и кондиционеры были выключены после прекращения подачи энергии.</p>
<p>Качество электроэнергии может быть очень разным даже в пределах одного города и района. Особенно сложна ситуация с электропитанием в промышленной зоне, где нередко арендуют офисы небольшие компании. Обилие энергоемкого промышленного оборудования, в том числе устаревшего и частично неисправного, вызывает регулярные отключения, всплески, шумы и перекосы фаз. Сплошь и рядом наблюдается ситуация, когда утренние "включения рубильника" на предприятии вызывают отключения электропитания в соседних фирмах и приводят к отказам техники.</p>
