<p>Данная статья посвящена нахождению корней систем линейных алгебраических уравнений. Методы численного решения таких систем подразделяются на два типа: прямые (конечные) и итерационные (бесконечные). Оба метода полезны и удобны для практических вычислений, и каждый из них имеет свои преимущества и недостатки.</p>

<br>
<h3>1. Метод исключений (метод Гаусса).</h3>

<p>Данный метод является наиболее известным и широко применяемым.</p>
<p>Рассмотрим систему из n уравнений с n неизвестными. Обозначим неизвестные через x[1], x[2], ... , x[n] и запишем систему в следующем виде:</p>
<pre class="outpp">
     a[1,1]*x[1] + a[1,2]*x[2] + ... + a[1,n]*x[n] = b[1]
     a[2,1]*x[1] + a[2,2]*x[2] + ... + a[2,n]*x[n] = b[2]
     ....................................................
     a[n,1]*x[1] + a[n,2]*x[2] + ... + a[n,n]*x[n] = b[n]
</pre>

<p>Предполагается, что в силу расположения уравнений, a[i,i] <> 0.</p>
<p>Если это не так, то, меняя уравнения местами, добиваемся выполнения этого условия. Введем n-1 множителей:</p>
<pre class="outpp">
     m[i] = a[i,1]/a[1,1]   , где i = 2, 3, ... , n
</pre>

<p>и вычтем из каждого i-го уравнения первое, умноженное на m, обозначая:</p>
<pre class="outpp">
     a\'[i,j] = a[i,j] - m[i]*a[1,j]
     b\'[i] = - m[i]*b[i]
     i = 2, 3, ... , n;  j = 1, 2, ... , n.
</pre>

<p>Для всех уравнений, начиная со второго, получим:</p>
<pre class="outpp">
     a[i,1] = 0   , где i = 2, 3, ... , n.
</pre>

<p>Преобразованная система уравнений запишется в следующем виде:</p>
<pre class="outpp">
     a[1,1]*x[1] + a[1,2]*x[2] + ... + a[1,n]*x[n] = b[1]
        0   +     a\'[2,2]*x[2] + ... + a\'[2,n]*x[n] = b\'[2]
     ....................................................
        0   +     a\'[n,2]*x[2] + ... + a\'[n,n]*x[n] = b\'[n]
</pre>

<p>Продолжая таким же образом, исключаем x[2] из последних n-2 уравнений, затем - x[3] из последних n-3 и т.д. На некотором k-м этапе мы исключаем x[k] с помощью множителей:</p>
<pre class="outpp">
     m^(k-1)_[i] = a^(k-1)_[i,k]/a^(k-1)_[k,k]   , i = k+1, ... , n,
</pre>

<p>не забывая, что a(k-1)[k,k] <> 0.</p>
<br>
<div class=outp>
<table border="0" cellspacing=1 cellpadding=1>
 <tr>
  <td width=100><b>Примечание:</b></td>
  <td>^(k-1) обозначает "на итерации номер k"</td>
 </tr>
 <tr>
  <td width=100>&nbsp;</td>
  <td>_[i,k] - индекс элемента.</td>
 </tr>
</table>
</div>

<p>Тогда:</p>
<pre class="outpp">
     a^(k)_[i,j] = a^(k-1)_[i,j] - m^(k-1)_[i]*a^(k-1)_[k,j]
     b^(k)_[i] = - m^(k-1)_[i]*b^(k-1)_[k]
     i = k+1, k+2, ... , n;  j = k, k+1, ... , n.
</pre>

<p>Окончательно, треугольная система уравнений записывается:</p>
<pre class="outpp">
     a[1,1]*x[1] + a[1,2]*x[2] + ... + a[1,n]*x[n] = b[1]
                   a[2,2]*x[2] + ... + a[2,n]*x[n] = b[2]
     ....................................................
                                       a[n,n]*x[n] = b[n]
</pre>

<p>Обратная подстановка для нахождения значений неизвестных задается формулами:</p>
<pre class="outpp">
     x[n] = b^(n-1)_[n] / a^(n-1)_[n,n]
     x[n-1] = (b^(n-2)_[n-1] - a^(n-2)_[n-1,n]*x[n]) / a^(n-2)_[n-1,n-1]
     ...................................................................
     x[j] = ( b^(j-1)_[j] - a^(j-1)_[j,n]*x[n] - ... -
            - a^(j-1)_[j,j+1]*x[j+1] ) / a^(j-1)_[j,j]

     j = n-2, n-3, ... , 1.
</pre>

<p>Перед началом процесса исключения очередного неизвестного может понадобиться переставить уравнения в системе, чтобы избежать деления на ноль. Кроме этого, при перестановке уравнений необходимо добиваться выполнения неравенства:</p>
<pre class="outpp">
     | a^(k-1)_[k,k] | >= | a^(k-1)_[i,k] |
</pre>

<p>Поэтому перед началом процесса исключения очередного неизвестного необходимо переставить уравнения так, чтобы максимальный по модулю коэффициент при x[k] попал на главную диагональ (данный способ решения часто называется "методом главного элемента").</p>

<br>
<h3>2. Итерационный метод Гаусса-Зейделя.</h3>
<p>Данный метод отличается малой ошибкой округления, простотой и легкостью программирования, но сходится только при выполнении некоторых условий.</p>
<p>Рассмотрим все ту же систему из n уравнений с n неизвестными.</p>
<p>По-прежнему полагаем, что a[i,i] <>0 для всех i. Тогда k-е приближение к решению будет задаваться формулой:</p>
<pre class="outpp">
  x^(k)_[i] = ( b[i] - a[i,1]*x^(k)_[1] - ... - a[i,j-1]*x^(k)_[i-1] -
              - a[i,j+1]*x^(k-1)_[i+1] - a[i,n]*x^(k-1)_[n] ) / a[i,i]
  i = 1, 2, ... , n.
</pre>

<p>Как известно, любой итерационный процесс должен иметь конец. Поэтому вычисления нужно продолжать до тех пор, пока все x^(k)_[i] не станут достаточно близки к x(k-1)[i], т.е. пока для всех i не будет выполняться неравенство:</p>
<pre class="outpp">
     max| x^(k)_[i] - x^(k-1)_[i] | < Eps,
</pre>

<p>где Eps некоторое положительное число, задающее точность вычислений.</p>
<p>Итерационный метод Гаусса-Зейделя сходится, если удовлетворяется достаточное условие сходимости: для всех i модуль диагонального коэффициента должен быть не меньше суммы модулей остальных коэффициентов данной строки:</p>
<pre class="outpp">
     | a[i,i] | >= | a[i,1] | + | a[i,2] | + ... + | a[i,j-1] | +
                   + | a[i,j+1] | + ... + | a[i,n] | ,


а хотя бы для одной строки это неравенство должно выполнятся строго:
     | a[i,i] | > | a[i,1] | + | a[i,2] | + ... + | a[i,j-1] | +
                  + | a[i,j+1] | + ... + | a[i,n] |.
</pre>

<br>
<h3>3. Сравнение методов.</h3>
<p>Метод исключений имеет то преимущество, что он конечен и, теоретически, с его помощью можно решить любую невырожденную систему. Итерационный метод сходится только для специальных систем уравнений, однако когда итерационные методы сходятся, они, как правило, предпочтительнее:</p>
<ul>
<li>Время вычислений пропорционально n2 на итерацию, в то время как для метода исключений время пропорционально n3. Если для решения системы требуется менее n итераций, то общие затраты машинного времени будут меньше.</li>
<li>Как правило, ошибки округления в итерационном методе меньше. Часто это соображение может оказаться достаточно важным, что оправдывает дополнительные затраты машинного времени. Многие системы, возникающие на практике, имеют среди коэффициентов большое кол-во нулей. В этом случае итерационные методы, если они сходятся, в высшей степени предпочтительны, так как в методе исключений получается треугольная система, которая обычно не содержит нулей в качестве коэффициентов.</li>
</ul>

