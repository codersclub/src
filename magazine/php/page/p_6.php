&nbsp;&nbsp;BmpSize: SIZE;
&nbsp;&nbsp;OldObj: HGDIOBJ;
&nbsp;&nbsp;OldOrg: TPoint;
begin
&nbsp;&nbsp;OldObj := SelectObject(DC, Brush);
&nbsp;&nbsp;//Получить размер рисунка.
&nbsp;&nbsp;GetBitmapDimensionEx(Bitmap, BmpSize);
&nbsp;&nbsp;if (Pattern = -1) then
&nbsp;&nbsp;begin //Отобразить весь рисунок.
&nbsp;&nbsp;&nbsp;&nbsp;SetBrushOrgEx(DC, X, Y, @OldOrg);
&nbsp;&nbsp;&nbsp;&nbsp;PatBlt(DC, X, Y, BmpSize.cX, BmpSize.cY, PATCOPY);
&nbsp;&nbsp;end
