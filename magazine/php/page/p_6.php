while read line
do
  echo $line | grep "subtitle=\"*\"" >/dev/null 2>&1 && \
      echo $line | \
    	sed \'s/.*&lt;seq.*subtitle=\"\([^"]*\)\".*/\1/\' | \
	w3m -dump -T text/html
done
</pre>

