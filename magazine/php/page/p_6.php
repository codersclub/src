<p>3. Отображение рисунка частями (pattern drawing).</p>
<p>Часто требуется отобразить не весь рисунок, а только какую-либо его часть. Примеров куча: графические редакторы, игры и т. п.</p>
<p>Для следующего примера потребуется переменная Brush типа HBrush, инициализированная с помощью функции CreatePatternBrush(Handle), где Handle - это описатель битмапа. Удаляется эта кисть с помощью DeleteObject(Brush). Можно это делать прямо в процедуре, но тогда сильно снизится скорость. Также перед этим должна быть вызвана функция SetBitmapDimensionEx для того, чтобы процедура могла получить размер рисунка. Эту функцию (SetBitmapDimensionEx) достаточно вызвать только один раз, при изменении размеров рисунка.</p>

<pre class="code">procedure DrawPattern(DC: HDC; X, Y: Integer; PatWidth, PatHeight: Integer;
&nbsp;&nbsp;Brush: HBRUSH; Bitmap: HBITMAP; Pattern: Integer = -1);
