<h2>Глава 1. Математические функции и процедуры.</h2>

<p>Математика - сложная, но требуемая в программировании вещь. Тот, кто плохо знает математику, вряд ли сможет написать даже простой шутер - нужны поворот за мышью, стрельба, движение... Это все требует тригонометрических расчетов и многого другого. В общем, смотрите.</p>

<p>1. Константы.</p>
<p>В некоторых функциях нельзя обойтись без этих констант. Хотя можно использовать их альтернативы, например, функцию DegToRad, их использование в чем-то быстрее (не нужно дополнительное деление, к примеру).</p>

<pre class="code">const
&nbsp;&nbsp;PiDiv2 = Pi / 180;</pre>

<p>Константа PiDiv2 используется для перевода градусов в радианы и обратно. Для первого умножайте градусы на это число, для второго - делите.</p>

<p>2. Поворот точки.
<p>Самая простая вещь. На эту тему говорилось много, но вдруг кто не знает.

<pre class="code">function RotateX(Angle, X, Y: Real; Xc: Real = 0; Yc: Real = 0): Real;
var
&nbsp;&nbsp;Radians: Real;
begin
&nbsp;&nbsp;Radians := Angle * PiD2;
&nbsp;&nbsp;RotateX := (X - Xc) * Cos(Radians) - (Y - Yc) * Sin(Radians) + Xc;
end;

function RotateY(Angle, X, Y: Real; Xc: Real = 0; Yc: Real = 0): Real;
var
&nbsp;&nbsp;Radians: Real;
