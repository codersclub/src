<h2>Формат машинных команд IA-32</h2>
<p>В настоящее время существует несколько сотен (если не больше) языков программирования, но почти все компиляторы транслируют программы в язык ассемблера. Ассемблер, в свою очередь, транслирует программу в особые комбинации нулей и единиц - машинный язык (язык машинных команд), единственный язык, который понимает компьютер. На первых компьютерах все программы писались на машинном языке, но в 50-х гг. стали использовать его символический аналог - язык ассемблера.</p>
<p>Давайте посмотрим, каким машинных командам соответствуют команды ассемблера. Рассмотрим простейшую команду:</p>

<pre class="code">
mov ebx, eax
</pre>

<p>На машинном языке это будет выглядеть так:</p>

<pre class="code">
8B D8
</pre>

<p>Здесь 8B - код операции. Рассмотрим другой пример:</p>

<pre class="code">
mov ecx, 128
</pre>

<p>На машинном языке ей соответствует:</p>

<pre class="code">
B9 00000080
</pre>

<p>Код операции - B9. Можно сделать вывод, что прямого соответствия между командой ассемблера и соответствующей машинной командой нет. Команда в примере одна и та же, а коды операций машинного языка - разные. Дело в том, что большинство команд ассемблера имеют несколько возможных вариантов сочетания операндов, а машинная команда всегда однозначна по отношению к производимым действиям на уровне аппаратуры. Код операции является номером микропрограммы в блоке микропрограммного управления. Для каждой конкретной команды ассемблера с конкретным вариантом сочетания операндов существует своя микропрограмма и свой код операции. </p>
<p>Любая команда ассемблера содержит несколько элементов:</p>
<ul>
<li>Элемент, описывающий, что делать, называется кодом операции (далее КОП).</li>
</li>Элементы, описывающие объекты, с которыми нужно что-то делать, называются операндами. Операнды могут не задаваться явно, а подразумеваться по умолчанию</li>
</li>Элементы, описывающие, как делать, называются типами операндов. Они обычно задаются неявно.</li>
</ul>

<h2>Команды передачи управления</h2>
<p>Последовательно работает очень мало программ. Обычно в программе есть точки, в которых принимается решение о том, какая команда будет выполняться следующей. Это решение может быть безусловным (управление передается конкретной команде, которая находится на некотором удалении от текущей) и условным (команда, которая будет выполняться следующей, определяется на основе анализа некоторых условий и данных). Но каким же образом обозначается место, куда необходимо передать управление? Оно обозначается с помощью меток. Метка - символическое имя определенной ячейки памяти, предназначенное для использования в командах передачи управления в качестве операнда. Транслятор ассемблера присваивает каждой метке три атрибута:</p>
<ul>
<li>Имя сегмента кода, в котором эта метка описана</li>
<li>Смещение, то есть расстояние в байтах от начала этого сегмента</li>
<li>Тип метки</li>
</ul>

<h2>Метки бывают двух типов:</h2>
<ul>
<li>near. Переход на такую метку возможен только в пределах того сегмента кода, в котором она описана</li>
<li>far. Переход на метку возможен в результате межсегментной передачи управления</li>
</ul>

<p>Определить метку можно двумя способами:</p>
<ul>
<li>с помощью директивы LABEL</li>
<li>с помощью оператора ":" (двоеточие).</li>
</ul>

<p>Используя оператор ":", можно определить метку только ближнего типа (near). Например:</p>

<pre class="code">
m:
mov eax,1
</pre>

<p>Используя оператор LABEL, можно определить метку любого типа. Например:</p>

<pre class="code">
m label near
mov eax,1
</pre>

<p>или</p>

<pre class="code">
m label far
mov eax,1
</pre>

<p>Применение меток оправдано во многих случаях. В одних лучше использовать метки ближнего типа, в других - дальнего. В следующих уроках мы рассмотрим эти случаи и научимся правильно выбирать тип метки.</p>
<p>Синтаксис команды безусловного перехода такой:</p>

<pre class="code">
jmp [модификатор] адрес_перехода 
</pre>

<p>Существует несколько кодов машинных команд, соответствующих JMP. Они отличаются дальностью перехода и способом задания целевого адреса. Операнд адрес_перехода может находиться в текущем сегменте(близкий переход) кода или в другом (дальний переход). Адресом перехода может являться имя метки:</p>

<pre class="code">
jmp m
m:
mov eax,1
</pre>

<p>Есть еще такой вариант (указывается не адрес перехода, а место, где он хранится):</p>

<pre class="code">
lea eax,m
jmp eax
m:
mov eax,1
</pre>
 
<p>Теперь перейдем к командам условного перехода. В таких командах решение о том, какая команда будет выполняться следующей, принимается в зависимости от некоторых условий, различных для разных команд перехода . Таких команд 18, они позволяют проверить:</p>
<ul>
<li>отношение операндов со знаком (больше либо меньше)</li>
<li>отношение операндов без знака (выше или ниже)</li>
<li>состояние арифметических флагов ZF, SF, CF, OF, PF(но не AF)</li>
</ul>

<p>Все команды условного перехода имеют одинаковый синтаксис:</p>

<pre class="code">
jcc метка_перехода
</pre>

<p>Любая из них начинается с символа "j" (от слова  jump), а вместо "cc" указывается конкретное условие, которое должна проанализировать программа. Метка перехода может находиться только в пределах текущего сегмента! Поэтому необходимость в модификаторе отпадает (всегда near). Условие обязательно должно быть сформировано до команды передачи управления. Его источником может служить любая команда, изменяющая состояние арифметических флагов или регистра ECX/CX.</p>
<p>В большинстве случаев Вы будете пользоваться командой CMP. Поэтому сейчас я немного расскажу вам о ней. Команда CMP производит практически те же действия, что и команда SUB, только она не записывает результат вычитания на место первого операнда, а просто устанавливает флаги (нас интересует флаг знака результата, и флаг нулевого результата). Вот ее синтаксис:</p>

<pre class="code">
cmp операнд_1, операнд_2
</pre>

