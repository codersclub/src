&nbsp;&nbsp;&nbsp;&nbsp;SetBrushOrgEx(DC, X - (Pattern mod (BmpSize.cX div PatWidth)) * PatWidth,
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Y - (Pattern div (BmpSize.cX div PatWidth)) * PatHeight, @OldOrg);
&nbsp;&nbsp;&nbsp;&nbsp;PatBlt(DC, X, Y, PatWidth, PatHeight, PATCOPY);
&nbsp;&nbsp;end;
&nbsp;&nbsp;SetBrushOrgEx(DC, OldOrg.X, OldOrg.Y, nil);
&nbsp;&nbsp;SelectObject(DC, OldObj);
