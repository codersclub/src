begin
&nbsp;&nbsp;RotatePoint.X := Round(RotateX(Angle, Point.X, Point.Y, Xc, Yc));
&nbsp;&nbsp;RotatePoint.Y := Round(RotateY(Angle, Point.X, Point.Y, Xc, Yc));
