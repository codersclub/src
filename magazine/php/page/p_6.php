<h2>Сброс материала на компьютер</h2>
<p>Теперь пришло время сбросить отснятый материал на компьютер. Для этого необходимо иметь не очень старый компьютер, оборудованный интерфейсом IEEE 1394 и имеющий вместительный жёсткий диск. Час DV материала занимает не больше 15 GB. Я использую для работы с видео достаточно древний компьютер: Intel Celeron 900 / 256 MB RAM / 140 GB HDD / PCI IEEE 1394 Interface / ALTLinux 2.4 Master</p>
<p>Для сброса материала используется программа <noindex><a href="http://kino.sf.net/" rel="nofollow">Kino</a></noindex>. Мне нравится работать с GUI, консоль оставим для других задач. Но если что-то вам мешает использовать графический интерфейс на данном этапе, то обратитесь к программе dvgrab.</p>
<p>Подключаем DV камеру к компьютеру. Я специально пропускаю момент настройки интерфейса IEEE 1394, это неоднократно и подробно описано в <noindex><a href="http://www.google.ru/linux/" rel="nofollow">Google</a></noindex>. К тому же на ALTLinux эта плата определяется автоматически. А вот список модулей следует привести:</p>
