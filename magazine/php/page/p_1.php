<h2>Преобразование строк из одного типа в другой</h2>
<p>Здесь, все как обычно, и просто и сложно.</p>
<p>Преобразование между "настоящими" строковыми типами String[n], ShortString, и AnsiString выполняются легко, и прозрачно. Никаких явных действий делать не надо, Delphi все сделает за Вас. Надо лишь понимать, что в маленькое большое не влезает. Например:</p>
<pre class=code>
var
  s3 :String[3];
  s  :AnsiString;
...
  s := \'abcdef\';
  s3 := s;
</pre>
<p>В результате выполнения этого кода, в переменной s3 окажется строка \'abc\', а не \'abcdef\'. С преобразованием из pChar в String[n], ShortString, и AnsiString, тоже всё очень не плохо. Просто присваивайте, и все будет нормально.</p>
<p>Сложности начинаются тогда, когда мы начинаем преобразовывать "настоящие" строковые типы в pChar. Непосредственное присваивание переменным типа pChar значений строк не допускается компилятором. На оператор p := s где p имеет тип pChar, а s :AnsiString, компилятор выдаст сообщение: "Incompatible types: \'String\' and \'PChar\'" - несовместимые типы \'String\' и \'PChar\'. Чтобы избежать такой ошибки, надо применять явное приведение типа: p := pChar(s). Так рекомендуют разработчики Delphi. В общем, они правы. Но, если вспомнить, как хранятся динамические строки - с нулем в конце, как и pChar. А еще и то, что к AnsiString применимо преобразование в тип Pointer. Станет очевидным, что всего, возможно целых три способа преобразования строки в pChar:</p>
<pre class=code>
var
  s :AnsiString;
  p1,p2,p3 :PChar;
...
  p1 := pChar(s);
  p2 := Pointer(s);
  p3 := @(s[1]);
</pre>
<p>Все они, синтаксически правильны. И кажется, что все три указателя (p1, p2 и p3) будут в результате иметь одно и то же значение. Но это не так. Всё зависит от того, что находится в s. Если быть более точным, равно ли значение s пустой строке, или нет:</p>
<pre class=code>
s <> \'\'
p1  =  p2 <> p3
s = \'\'
p1 <> p2  =  p3
</pre>
<p>Чтобы Вы понимали причину такого явления, я опишу, как Delphi выполняет каждое из этих преобразований. В начале напомню, что переменные AnsiString представляющие пустые строки, реально имеют значение Nil. Так вот:</p>
<pre class=code>
pChar(s)
</pre>
<p>Для выполнения преобразования pChar(s), компилятор генерит вызов специальной внутренней функции @LstrToPChar. Эта функция проверяет – если строковая переменная имеет значение Nil, то вместо него, она возвращает указатель на реально размещенную в памяти пустую строку. Т.е. pChar(s) никогда не вернет указатель равный Nil.</p>
<pre class=code>
Pointer(s)
</pre>
<p>Тут все просто, такое преобразование просто возвращает содержимое строковой переменной. Т.е. если она при пустой строке содержит Nil, то и результатом преобразования будет Nil. Если же строка не пуста, то результатом будет адрес экземпляра строки.</p>
<pre class=code>
@(s[1])
</pre>
<p>Здесь, все совсем по другому. Перед выполнением такого преобразования, компилятор вставляет код, обеспечивающий ситуацию, когда указатель, хранящийся в s, будет единственным указателем на экземпляр строки в памяти. В нашем примере, если строка не пуста, то будет создана копия исходной строки. Вот ее-то адрес и будет возвращен как результат такого преобразования. Но, если строка пуста, то результатом будет Nil, как и во втором случае.</p>
<p>Теперь, интересно отметить, что если в приведенном примере, преобразование p3 := @(s[1]) выполнить первым, то при не пустой строке в s, все указатели (p1, p2, и p3), будут равны. И содержать они будут адрес "персонального" экземпляра строки.</p>
<p>Вот, теперь, зная как выполняются те или иные преобразования, Вы сможете всегда выбрать подходящий способ. Для тех, кто ещё не "проникся" до конца этой проблемой, приведу пример. В нем, преобразование, рекомендуемое разработчиками, приводит к "странному" поведению программы:</p>
<pre class=code>
procedure X1;
var
  s :AnsiString;
  p :PChar;
begin
  s := \'abcd\';
  p  := PChar(s);
  p^ := \'X\'; // <- 
  ShowMessage(s);
end;
</pre>
<p>вызывает ошибку доступа к памяти при выполнении строки, помеченной <=. Почему - предлагаю Вам разобраться самостоятельно. После прочтения данной статьи Ваших знаний для этого достаточно. В тоже время, код:</p>
<pre class=code>
procedure X1;
var
  s :AnsiString;
  p :PChar;
begin
  s := \'abcd\';
  p  := @(s[1]);
  p^ := \'X\';
  ShowMessage(s);
end;
</pre>
<p>будет выполнен без ошибок, выведя строку \'Xabcd\'. Также как и код:</p>
<pre class=code>
procedure X1;
var
  s :AnsiString;
  p :PChar;
begin
  s := \'abcd\';
  s[2] := \'b\';
  p  := PChar(s);
  p^ := \'X\'; 
  ShowMessage(s);
end;
</pre>
<p>Рассматривая преобразование AnsiString в pChar (получение адреса строки) нельзя не упомянуть ещё одну серьезную проблему – область действия для полученного таким путем указателя.</p>
<p>Как вы, наверное, еще помните, Delphi ведет учет всех ссылок на каждый экземпляр динамически распределенной строки. И на основании этого принимает решение об освобождении памяти занимаемой экземпляром строки. Но, это касается только ссылок хранящихся в переменных AnsiString. Ссылки, полученные преобразованием в pChar, не учитываются. Вот демонстрация того, как это может сказаться на поведении программы. Пусть есть следующая функция:</p>
<pre class=code>
function IntToPChar (n :Integer) :pChar;
var s :AnsiString;
begin
  s := IntToStr(n);
  Result := PChar(s);
end;
</pre>
<p>Казалось бы, всё написано правильно. Однако если выполнить такой оператор:</p>
<pre class=code>
  ShowMessage(IntToPChar(100));
</pre>
<p>То, вместо ожидаемого окна со строкой \'100\', мы либо получим абракадабру, либо и того хуже – ошибку AV. А все почему? Да, просто, единственным учтённым указателем на экземпляр строки, полученный от IntToStr, будет s. Поэтому, когда его область действия прекращается по выходу из процедуры, экземпляр строки будет уничтожен. А не учтённый указатель, возвращаемый функцией IntToPChar, после этого станет указывать "куда бог пошлёт". Точнее, на то место в памяти, где недавно была строка. Если же переменная s будет объявлена на глобальном уровне, то будет нормально, но всё равно возможны ошибки. Например:</p>
<pre class=code>
var s :AnsiString; // глобальная переменная
...
function IntToPChar (n :Integer) :pChar;
begin
  s := IntToStr(n);
  Result := PChar(s);
end;
...
var p100, p200 :pChar;
begin
  p100 := IntToPChar(100);
  p200 := IntToPChar(200);
...
</pre>
<p>После второго выполнения функции IntToPChar, указатель p100, опять будет указывать "куда бог пошлет", Почему, разберитесь сами :).</p>
<p>В завершении этого раздела, я хочу "напугать" Вас:</p>
<p>Будьте крайне осторожны и бдительны при получении и использовании ссылок pChar на динамически размещенные строки,</p>
<p>Знание того, как это все работает, думаю, Вам поможет.</p>

<h2>Функции для работы со строками, о которых многие часто забывают, или вовсе не знают</h2>
<p>Как ни странно, но очень часто в форумах можно встретить вопросы, а главное ответы на них, показывающие, что многие программисты, пишущие на Delphi не знают о существовании некоторых, весьма полезных функций.</p>

<p>В этой части статьи я кратко расскажу о тех, которые, на мой взгляд, бывают полезны. Я не буду подробно о них рассказывать. Точное их описание Вы сможете найти в справочной системе. Здесь же я лишь упомяну о том, как обычно их использую я, и для некоторых из них приведу пример использования из реальной жизни. Рассматривайте данный раздел, как ссылки на функции, о которых вам следует почитать в справочной системе.</p>
<pre class=code>
procedure SetLength (var S; NewLength: Integer)
</pre>
<p>Позволяет распределить память для динамической строки. Замечу, если строка уже содержит некоторое число символов, они копируются в результирующий экземпляр. Если конечно влезут. Это позволяет "расширять" уже существующий экземпляр строки.</p>
<p>Очень часто я использую эту функцию, когда хочу использовать динамическую строку как буфер. Например, при вызове функций Windows API:</p>
<pre class=code>
function ComputerNetName :String;
// Возвращает сетевое имя компьютера
var Len :Cardinal;
begin
  Len := MAX_COMPUTERNAME_LENGTH+1;
  SetLength(Result,Len); // выделить память для буфера
  if  GetComputerName(pChar(Result),Len)  then 
    SetLength(Result,Len) // установить точный размер 
  else
    Result := \'\';
end;

procedure SetString(var s: string; buffer: PChar; len: Integer);
</pre>
<p>Распределяет память под экземпляр строки, копирует в него len байт из буфера buffer (если он не Nil), и присваивает ссылку на этот экземпляр переменной s. После вызова этой функции, значение счётчика ссылок полученного экземпляра всегда равно 1.</p>
<p>Её удобно использовать, когда нужно преобразовать в строку массив символов, не заканчивающийся #0.</p>
<pre class=code>
function SysErrorMessage(ErrNo: Integer): string;
// Получение описания ошибки по коду
var
  Size   :Integer;
  Buffer :array[0..1024] of Char;
begin
  Size := FormatMessage
    (FORMAT_MESSAGE_FROM_SYSTEM or FORMAT_MESSAGE_ARGUMENT_ARRAY
    ,nil ,ErrNo ,0 ,@Buffer,SizeOf(Buffer) ,nil
    );
  SetString(Result, Buffer, Size);
end;

procedure UniqueString (var str: string)
</pre>
<p>Гарантирует что после вызова, str будет указывать на уникальный экземпляр значеничя строки. Т.е.,  str будет указывать на строку счетчик ссылок которой будет равен 1. Если необходимо, делает копию исходного значения. Обычно нет необходимости использовать эту функцию. Однако, иногда мне надо получить указатель на строку (pChar), для того что бы изменить её. А поскольку, как я уже говорил, указатели pChar не обеспечиваются контролем за изменением строк. Это может привести к побочным изменениям в "других" строках, если счётчик ссылок исходной строки был >1. Вот и приходится применять эту функцию.</p>
<pre class=code>
procedure Wr   (Msg  :String);
// вывод строки в консольное окно в OEM кодировке
begin
  UniqueString(Msg); // получить "персональный" экземпляр строки
  Windows.CharToOem(pChar(Msg),pChar(Msg)); // преобразование его
  Write(Msg); // вывод преобразованного экземпляра на консоль
end;
</pre>
<p>Здесь, поскольку параметр передаётся по значению, при вызове будет создана локальная копия переменной (указателя) Msg. Об этом я ещё расскажу ниже. Эта переменная, при завершении процедуры будет уничтожаться, что приведёт к освобождению "персональной" копии экземпляра переданной и преобразованной строки.</p>
<pre class=code>
function AnsiCompareStr(const S1, S2: string): Integer; 
function AnsiCompareText(const S1, S2: string): Integer; 
function CompareStr(const S1, S2: string): Integer; 
function CompareText(const S1, S2: string): Integer;
</pre>
<p>Функции этой группы используются для сравнения строк. Результатом будет одно из значений:</p>
>0 если S1 > S2<br>
<0 если S1 < S2<br>
=0 если S1 = S2<br>
<p>Особенно удобно использование этих функций при реализации алгоритмов сортировки.</p>
<p>Разница между ними следующая:</p>
<p>Те, чьё имя заканчивается на Str, выполняют точное сравнение с учётом регистра символов, а те, что заканчиваются на Text, выполняют сравнение без учета регистра символов.</p>
<p>Те чьё имя начинается на Ansi, сравнивают с учётом упорядоченности символов определяемой текущей языковой настройкой Windows. Функции без Ansi, сравнивают символы как 8-битные значения, что для некоторых языковых кодировок может дать не совсем правильные результаты. В общем случае, при таком сравнении, правильным можно считать лишь результат сравнения строк содержащих только латинские символы. Например, если сравниваемые строки содержат русскую буквы \'Ё\' или \'ё\', то результат сравнения будет неправильным. Поэтому, я практически всегда использую функции с префиксом Ansi. Даже учитывая то, что они работают чуть медленнее.</p>
<p>Такой же принцип формирования имён используется и в следующих функциях. Поэтому, я больше не буду останавливаться на различиях подобных функций.</p>
