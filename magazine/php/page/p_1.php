<H2>Ортогональные тензоры. Тензор поворота</H2>
<P>Важным классом тензоров второго ранга являются так называемые ортогональные 
тензоры.</P>
<P>&nbsp;</P>
<P><B><I>Определение:</I></B> <I>тензор второго ранга </I><B><I>Q</I></B><I> 
называется ортогональным, если выполняется равенство</I></P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle 
      width="95%"><B><I>Q</I></B>·<B><I>Q</I></B><I><SUP>T</SUP></I> = 
      <B><I>1</I></B>. </TD>
    <TD align=right width="5%">(26) </TD></TR></TBODY></TABLE>
<P>Легко показать, что линейное преобразование 
<B><I>y</I></B>&nbsp;=&nbsp;<B><I>Q</I></B>&nbsp;·&nbsp;<B><I>x</I></B> не 
меняет длин векторов, то есть 
|&nbsp;<B><I>y&nbsp;</I></B>|&nbsp;=&nbsp;|&nbsp;<B><I>x&nbsp;</I></B>|. Этот 
факт также может быть положен в основу определения ортогонального тензора. Из 
(26) следует, что тензор <B><I>Q</I></B> невырожден, т.к.</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>Q</I></B><SUP>–1</SUP> = 
      <B><I>Q</I></B><I><SUP>T</SUP></I>, </TD>
    <TD align=right width="5%">(27) </TD></TR></TBODY></TABLE>
<P>а транспонированный тензор существует всегда. Вычислим определитель 
<B><I>Q</I></B></P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">det <B><I>1</I></B> = 
      det(<B><I>Q</I></B>·<B><I>Q</I></B><I><SUP>T</SUP></I>) = 
      (det(<B><I>Q</I></B>))<SUP>2</SUP> = 1&nbsp; =&gt;&nbsp; 
      det<B><I>Q</I></B> = ±1. </TD>
    <TD align=right width="5%">(28) </TD></TR></TBODY></TABLE>
<P>Таким образом, все множество ортогональных тензоров разбивается на два 
подмножества. Элементы множества тензоров, у которых 
det<B><I>Q&nbsp;</I></B>=&nbsp;+1, называются собственно ортогональными 
тензорами или <B><I>тензорами поворота</I></B>. Произвольный ортогональный 
тензор либо является тензором поворота, либо произведением тензора поворота на 
тензор инверсии (–<B><I>1</I></B>). </P>
<P>Как уже было сказано, тензор поворота не меняет длин векторов. А что 
происходит с углами? Пусть</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>a</I></B> &#8242; = <B><I>Q</I></B> · 
      <B><I>a</I></B>, &nbsp; <B><I>b</I></B> &#8242; = <B><I>Q</I></B> · 
      <B><I>b</I></B>. </TD>
    <TD align=right width="5%">(29) </TD></TR></TBODY></TABLE>
<P>Тогда</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>a</I></B> &#8242; · <B><I>b</I></B>&#8242; = 
      <B><I>a</I></B> · <B><I>Q</I></B><I><SUP>T</SUP></I>·<B><I>Q</I></B> · 
      <B><I>b</I></B> = <B><I>a</I></B> · <B><I>b</I></B>. </TD>
    <TD align=right width="5%">(30) </TD></TR></TBODY></TABLE>
<P>Т.е. при действии тензора поворота на два вектора углы между ними 
сохраняются. Почему же у истинного тензора поворота 
det<B><I>Q&nbsp;</I></B>=&nbsp;+1? Ответ таков, в этом случае происходит только 
поворот, в случае же det<B><I>Q&nbsp;</I></B>=&nbsp;–1 происходит поворот с 
отражением.</P>
<P>Одним из наиболее простых общих представлений тензора поворота является 
следующее. Возьмем произвольную ортонормированную тройку векторов 
<B><I>d</I></B><SUB>1</SUB>, <B><I>d</I></B><SUB>2</SUB>, 
<B><I>d</I></B><SUB>3</SUB>. При действии тензора <B><I>Q</I></B> они переходят 
также в ортонормированную тройку <B><I>D</I></B><SUB>1</SUB>, 
<B><I>D</I></B><SUB>2</SUB>, <B><I>D</I></B><SUB>3</SUB>:</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>D</I></B><I><SUB>i</SUB></I> = 
      <B><I>Q</I></B> · <B><I>d</I></B><I><SUB>i</SUB></I>&nbsp;&nbsp; 
      =&gt;&nbsp;&nbsp; <B><I>D</I></B><I><SUB>i</SUB><B>d</B><SUB>i</SUB></I> = 
      (<B><I>Q</I></B> · 
      <B><I>d</I></B><I><SUB>i</SUB></I>)<B><I>d</I></B><I><SUB>i</SUB></I> = 
      <B><I>Q</I></B>·(<B><I>d</I></B><I><SUB>i</SUB><B>d</B><SUB>i</SUB></I>) = 
      <B><I>Q</I></B>·<B><I>1</I></B> = <B><I>Q</I></B>. </TD>
    <TD align=right width="5%">(31) </TD></TR></TBODY></TABLE>
<P>В формуле (31) использовано так называемое <I>правило суммирования по 
Эйнштейну</I>. Подразумевается суммирование от 1 до 3 по повторяющемуся индексу. 
Например,</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle 
      width="95%"><B><I>d</I></B><I><SUB>i</SUB></I><B><I>d</I></B><I><SUB>i</SUB></I> 
      = <B><I>d</I></B><SUB>1</SUB><B><I>d</I></B><SUB>1</SUB> + 
      <B><I>d</I></B><SUB>2</SUB><B><I>d</I></B><SUB>2</SUB> + 
      <B><I>d</I></B><SUB>3</SUB><B><I>d</I></B><SUB>3</SUB>. </TD>
    <TD width="5%">&nbsp; </TD></TR></TBODY></TABLE>
<P>&nbsp;</P>
<P>В (31) использовано общее представление единичного тензора 
<B><I>d</I></B><I><SUB>i</SUB></I><B><I>d</I></B><I><SUB>i</SUB></I> = 
<B><I>1</I></B>. Чтобы объяснить, почему этот так нужно перейти от прямого 
тензорного представления к координатному. В этой статье мы этого не делаем, и 
надеемся, что читатель поверит нам на слово. Таким образом, достаточно общим 
представлением тензора поворота является</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>Q</I></B> = 
      <B><I>D</I></B><I><SUB>i</SUB><B>d</B><SUB>i</SUB></I> = 
      <B><I>D</I></B><SUB>1</SUB><B><I>d</I></B><SUB>1</SUB> + 
      <B><I>D</I></B><SUB>2</SUB><B><I>d</I></B><SUB>2</SUB> + 
      <B><I>D</I></B><SUB>3</SUB><B><I>d</I></B><SUB>3</SUB>. </TD>
    <TD align=right width="5%">(32) </TD></TR></TBODY></TABLE>
<P>Это представление понадобится нам при доказательстве теоремы Эйлера. А сейчас 
мы хотим обратить внимание на одно полезное тождество, справедливое для любого 
вектора <B><I>a</I></B> и тензора поворота <B><I>Q</I></B></P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>Q</I></B> · (<B><I>a</I></B> &#215; 
      <B><I>1</I></B>) · <B><I>Q</I></B><I><SUP>T</SUP></I> = (<B><I>Q</I></B> · 
      <B><I>a</I></B>) &#215; <B><I>1</I></B>. </TD>
    <TD align=right width="5%">(33) </TD></TR></TBODY></TABLE>
<H2>Теорема Эйлера</H2>
<P>Приведем пояснения к доказательству базовой теоремы в теории поворотов. Все 
остальные результаты теории получаются как следствия данной теоремы и при 
желании могут быть доказаны проницательным читателем самостоятельно.</P>
<P><B><I>Теорема Эйлера</I></B>: <I>произвольный тензор поворота</I> 
<B><I>P</I></B><I>, отличный от <B>1</B>, допускает единственное 
представление</I></P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> = <B><I>mm</I></B> + 
      cos&#952;(<B><I>1</I></B> – <B><I>mm</I></B>) + sin&#952;<B><I>m</I></B> &#215; 
      <B><I>1</I></B>, </TD>
    <TD align=right width="5%">(34) </TD></TR></TBODY></TABLE>
<P><I></I>&nbsp;</P>
<P>где <I>единичный вектор</I> <B><I>m</I></B><I> определяет прямую в 
пространстве, называемую осью поворота; </I>&#952;<I> называется углом поворота и 
считается положительным, если поворот при взгляде с конца вектора 
</I><B><I>m</I></B><I> происходит против хода часовой стрелки</I>.</P>
<P>&nbsp;</P>
<P><B><I>Пояснения к доказательству</I></B>: Покажем, что для тензора поворота 
<B><I>P</I></B>, существует единственный с точностью до множителя (±1) единичный 
вектор <B><I>m</I></B> (он называется неподвижным вектором тензора 
<B><I>P</I></B>), удовлетворяющий уравнению</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> · <B><I>m</I></B> = 
      <B><I>m</I></B>, | <B><I>m</I></B> | = 1. </TD>
    <TD align=right width="5%">(35) </TD></TR></TBODY></TABLE>
<P>Данное уравнение можно переписать в эквивалентной форме</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">(<B><I>P</I></B> – <B><I>1</I></B>) · 
      <B><I>m</I></B> = <B><I>0</I></B>, | <B><I>m</I></B> | = 1, </TD>
    <TD align=right width="5%">(36) </TD></TR></TBODY></TABLE>
<P>т.е. вектор <B><I>m</I></B> является решением однородного линейного 
уравнения. Из курса линейной алгебры известно, что однородная система линейных 
алгебраических уравнений имеет ненулевое решение тогда и только тогда, когда ее 
определитель равен нулю</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">det(<B><I>P</I></B> – <B><I>1</I></B>) = 0. 
    </TD>
    <TD align=right width="5%">(37) </TD></TR></TBODY></TABLE>
<P>Покажем, что это так. Для этого выпишем цепочку тождественных преобразований 
(использовано det(<B><I>P</I></B>) = 1)</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">det(<B><I>P</I></B> – <B><I>1</I></B>) = 
      det[<B><I>P</I></B>·(<B><I>1</I></B> – 
      <B><I>P</I></B><I><SUP>T</SUP></I>)] = <BR>= 
      det(<B><I>P</I></B>)det(<B><I>1</I></B> – 
      <B><I>P</I></B><I><SUP>T</SUP></I>) = det(<B><I>1</I></B> – 
      <B><I>P</I></B><I><SUP>T</SUP></I>) = <BR>= det(<B><I>1</I></B> – 
      <B><I>P</I></B>) = – det(<B><I>P</I></B> – <B><I>1</I></B>). 
      <P></P></TD>
    <TD align=right width="5%">(38) </TD></TR></TBODY></TABLE>
<P>Получили, что 
det(<B><I>P&nbsp;</I></B>–&nbsp;<B><I>1</I></B>)&nbsp;=&nbsp;–det(<B><I>P&nbsp;</I></B>–&nbsp;<B><I>1</I></B>), 
а это возможно, если верно равенство (37). Итак, ненулевое решение уравнения 
(36) существует. Понятно, что если <B><I>m</I></B> есть решение (36), то и 
(–<B><I>m</I></B>) есть решение этого уравнения. Однако оба эти вектора 
определяют одну и ту же прямую, натянутую на вектор <B><I>m</I></B>.</P>
<P>Далее методом «от противного» можно показать, что для тензора поворота 
<B><I>P</I></B>, отличного от <B><I>1</I></B>, существует только один 
неподвижный вектор ± <B><I>m</I></B>.</P>
<P>Выберем теперь правую ортонормированную тройку векторов 
<B><I>d</I></B><SUB>1</SUB>&nbsp;=&nbsp;<B><I>m</I></B>, 
<B><I>d</I></B><SUB>2</SUB>, <B><I>d</I></B><SUB>3</SUB>. Согласно (32) мы можем 
записать</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> = 
      <B><I>D</I></B><SUB>1</SUB><B><I>d</I></B><SUB>1</SUB> + 
      <B><I>D</I></B><SUB>2</SUB><B><I>d</I></B><SUB>2</SUB> + 
      <B><I>D</I></B><SUB>3</SUB><B><I>d</I></B><SUB>3</SUB> = 
      <B><I>D</I></B><SUB>1</SUB><B><I>m</I></B> + 
      <B><I>D</I></B><SUB>2</SUB><B><I>d</I></B><SUB>2</SUB> + 
      <B><I>D</I></B><SUB>3</SUB><B><I>d</I></B><SUB>3</SUB>, </TD>
    <TD align=right width="5%">(39) </TD></TR></TBODY></TABLE>
<P>где тройка <B><I>D</I></B><SUB>1</SUB>, <B><I>D</I></B><SUB>2</SUB>, 
<B><I>D</I></B><SUB>3</SUB> также является правой ортонормированной. Умножив 
равенство (39) скалярно на <B><I>m</I></B> справа и учтя (35), получим</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> · <B><I>m</I></B> = 
      <B><I>D</I></B><SUB>1</SUB> = <B><I>m</I></B>. </TD>
    <TD align=right width="5%">(40) </TD></TR></TBODY></TABLE>
<P>Таким образом, равенство (39) принимает вид</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> = <B><I>mm</I></B> + 
      <B><I>D</I></B><SUB>2</SUB><B><I>d</I></B><SUB>2</SUB> + 
      <B><I>D</I></B><SUB>3</SUB><B><I>d</I></B><SUB>3</SUB>. </TD>
    <TD align=right width="5%">(41) </TD></TR></TBODY></TABLE>
