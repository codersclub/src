<H3>4. Векторное произведение векторов</H3>
<P>Первые три закона композиции имеют место как в ориентированной, так и не в 
ориентированной системе отсчета. Четвертый закон композиции имеет место только в 
ориентированной системе отсчета. </P>
<P>Векторное произведение введем в два этапа. Пусть дана упорядоченная пара 
векторов <B><I>a</I></B> и <B><I>b</I></B>, где <B><I>a</I></B> считается первым 
(левым) сомножителем, а вектор <B><I>b</I></B> – вторым (правым). Сопоставим 
векторам <B><I>a</I></B> и <B><I>b</I></B> спин-вектор 
<B><I>c</I></B><SUB>*</SUB>&nbsp; такой, что</P>
<P style="TEXT-INDENT: 35.4pt">а) ось вектора <B><I>c</I></B><SUB>*</SUB> 
ортогональна плоскости, натянутой на векторы <B><I>a</I></B> и 
<B><I>b</I></B>;</P>
<P style="MARGIN-LEFT: 35.4pt">б) круговая стрелка показывает направление 
кратчайшего поворота от вектора <B><I>a</I></B> к вектору<B><I> b</I></B>;</P>
<P style="TEXT-INDENT: 35.4pt">в) модуль спин-вектора 
<B><I>c</I></B><SUB>*</SUB> равен</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">| <B><I>c</I></B><SUB>*</SUB> | = | 
      <B><I>a</I></B> | | <B><I>b</I></B> <B>|</B> sin &#952;, </TD>
    <TD align=right width="11%">(3) </TD></TR></TBODY></TABLE>
<P style="TEXT-INDENT: 35.4pt">где &#952; – угол кратчайшего поворота от 
<B><I>a</I></B> к <B><I>b</I></B>. </P>
<P>Для спин-вектора <B><I>c</I></B><SUB>*</SUB> введем обозначение 
<B><I>c</I></B><SUB>*</SUB>&nbsp;=&nbsp;<B>[<I>a</I></B>,&nbsp;<B><I>b</I>]</B>. 
Сразу можно отметить, что 
<B><I>c</I></B><SUB>*</SUB>&nbsp;=&nbsp;<B>[<I>a</I></B>,&nbsp;<B><I>b</I>]&nbsp;</B>=&nbsp;<B>–[<I>a</I></B>,&nbsp;<B><I>b</I>]</B>. 
Теперь осталось спин-вектору <B><I>c</I></B><SUB>*</SUB> поставить в 
соответствие прямой вектор <B><I>c</I></B>. Это можно сделать двумя способами в 
зависимости от выбранной нами ориентации в системе отсчета. Если мы работаем в 
<I>правоориентированной</I> системе отсчета, то при взляде с конца вектора 
<B><I>c</I></B> поворот от <B><I>a</I></B> к <B><I>b</I></B> будет 
осуществляться против часовой стрелки. Если же мы работаем в 
<I>левоориентированной</I> системе отсчета, то при взляде с конца вектора 
<B><I>c</I></B> поворот от <B><I>a</I></B> к <B><I>b</I></B> будет 
осуществляться по часовой стрелке. Чтобы не вносить путаницу, условимся всегда 
работать в <B><I>правоориентированной</I></B> системе отсчета.</P>
<P>Свойства векторного произведения:</P>
<P style="TEXT-INDENT: 35.4pt">а) 
<B><I>a</I></B>&nbsp;&#215;&nbsp;<B><I>b</I></B>&nbsp;=&nbsp;–&nbsp;<B><I>b</I></B>&nbsp;&#215;&nbsp;<B><I>a</I></B>;</P>
<P style="TEXT-INDENT: 35.4pt">б) 
<B><I>a</I></B>&nbsp;&#215;&nbsp;(<B><I>b</I></B>&nbsp;+&nbsp;<B><I>c</I></B>)&nbsp;=&nbsp;<B><I>a</I></B><B><I>&nbsp;</I></B>&#215;&nbsp;<B><I>b</I></B><B><I>&nbsp;</I></B>+&nbsp;<B><I>a</I></B><B><I>&nbsp;</I></B>&#215;&nbsp;<B><I>c</I></B> 
– дистрибутивность;</P>
<P style="TEXT-INDENT: 35.4pt">в) 
(<B><I>a</I></B>&nbsp;&#215;&nbsp;<B><I>b</I></B>)&nbsp;&#215;&nbsp;<B><I>c&nbsp;</I></B><SMALL>&#8800;</SMALL>&nbsp;<B><I>a&nbsp;</I></B>&#215;&nbsp;(<B><I>b&nbsp;</I></B>&#215;&nbsp;<B><I>c</I></B>) 
– ассоциативности нет!</P>
