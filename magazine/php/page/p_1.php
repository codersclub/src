<H2>Композиция поворотов. Правило квазикоммутативности</H2>
<P>Введем в рассмотрение понятие сложного поворота. Пусть задан вектор 
<B><I>a</I></B> и тензоры поворота <B><I>P</I></B><SUB>1</SUB> и 
<B><I>P</I></B><SUB>2</SUB>. Повернем вектор <B><I>a</I></B> тензором 
<B><I>P</I></B><SUB>1</SUB> и найдем вектор 
<B><I>a</I></B>&#8242;&nbsp;<SMALL>&#8801;</SMALL>&nbsp;<B><I>P</I></B><SUB>1</SUB>&nbsp;·&nbsp;<B><I>a</I></B>. 
Далее вектор <B><I>a</I></B>&#8242; повернем тензором <B><I>P</I></B><SUB>2</SUB> и 
найдем вектор 
<B><I>a</I></B>&#8242;&#8242;&nbsp;<SMALL>&#8801;</SMALL>&nbsp;<B><I>P</I></B><SUB>2</SUB>&nbsp;·&nbsp;<B><I>a</I></B>&#8242;. 
В результате вектор <B><I>a</I></B>&#8242;&#8242; вычисляется как сложный поворот вектора 
<B><I>a</I></B></P>
