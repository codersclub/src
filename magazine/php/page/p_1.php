<p>Целью данного обзора не является определение лучшего процессора или производителя, каждый рассмотренный процессор (мы рассмотрим далеко не все) внес новые технологии, переработанную систему команд, функции энергосбережения или другие новшества, которые отразились на последующих процессорах (или отразятся позже). Данная статья, скорее история развития микропроцессоров и усовершенствования основных характеристик, отличающих от аналогичных процессоров. Я пытался сохранить ход истории в правильном порядке, но некоторые даты могут не соответствовать действительности по ряду причин, например, анонсирование процессоров происходило в "горячке" с одним тестовым процессором на руках, а массовый выпуск данного процессора мог быть уже с усовершенствованным ядром месяцем позже (или через полгода). В этой статье рассмотрены процессоры 1-7 поколения, включая 32-разрядные современные процессоры. Следующее поколение процессоров 64-разрядные, будут рассмотрены в одной из следующих статей.</p>
<p>Начнем наш обзор с лидера и законодателя мод в мире процессоров - корпорации Intel, которая была основана в 1968 г. Первый процессор, разработанный специалистами этой корпорации, был i4040 в 1969 г. Он представлял собой 4-разрядное устройство с 2300 транзисторами (для примера: Pentium 4 имеет около 42 млн. транзисторов). Этот процессор применялся в карманных калькуляторах. В 1972 г. был выпущен 8-разрядный процессор i8008 с адресацией внешней памяти 16 Кбайт. Революцией можно считать 1974 г. – выпуск i8080. С этого момента начинается отсчет современных процессоров.</p>
<div align=center><img border=0 width=115 height=99 src="'. $site['setting']['base'] .'/img/m/0804/proc/proc1.jpg" alt="i8080"></div>
<p>Этот процессор мог адресовать 64 Кбайта и работал на тактовой частоте 2 МГц. Он разошелся миллионными тиражами и заложил основу во всю дальнейшую архитектуру процессоров.</p>
<p><strong>Первое поколение процессоров.</strong> Очередной революционный процессор Intel – i8086 – появился в 1978 г. Его основные характеристики – 16-разрядные регистры, 16-разрядная шина данных, сегментная адресация памяти 20 бит – это уже 1 Мбайт. Тактовая частота 4,77–10 МГц. Более дешевый вариант i8086 – это процессор i8088 – имеет 8 разрядную шину данных. Процессоры i8086/88 могли работать с внешним математическим сопроцессором i8087 (устанавливался в специальный разъем на плате). i8086 унаследовал большую часть множества команд 8080 и Z80. Все современные процессоры (в обязательном порядке) поддерживают набор команд процессора i8086, совместимость "снизу-вверх" - любую программу, написанную для i8086, можно запустить на Pentium 4 или Athlon XP.</p>
<p><strong>Второе поколение процессоров.</strong> Память в 1 Мбайт – была довольно долго большим объемом, но со временем ее оказалось мало. Для доступа к большему объёму памяти нужно было устанавливать драйвера расширенной памяти EMS, с помощью которых через окошко 64 Кбайта можно было получить доступ к 32 Мбайтам. В 1982 г. Intel представляет 80286 (в конце 80-х в СССР такой компьютер стоил как две машины "Волга", если кому нужен сейчас, могу продать по дешевке :-) ) с расширенной шиной 24 бита (16 Мбайт памяти) и защищенным режимом работы. До этого в процессорах отсутствовала поддержка на процессорном уровне защиты программ от взаимного влияния, такое нововведение стимулировало производителей программного обеспечения на выпуск многозадачных операционных систем (Windows, OS/2).</p>
<p><strong>Третье поколение процессоров.</strong> Действительно развитие многозадачности началось после выхода микропроцессора i80386 в 1985 г. Это первый 32-разрядный процессор, который положил начало семейству процессоров IA-32 (32-bit Intel Architecture). Главные отличительные особенности этого процессора: 32-разрядные шины адреса и данных (адресация 4 Гбайт); добавление 32-разрядных регистров; введен новый режим работы процессора – виртуальный 8086 процессор; страничная адресация памяти (стало возможно организовать виртуальную память). Введена концепция параллельного функционирования внутренних устройств процессора: шинный интерфейс, блок предварительной выборки, блок декодирования команд, исполнительный блок, блок сегментации, блок страничной адресации.</p>
<p><strong>Четвертое поколение процессоров.</strong> Концепция параллельного функционирования внутренних устройств нашла свое дальнейшее развитие в процессоре i80486 (1989 г., модели SX, SX2, DX, DX2, DX4) в виде конвейеризации вычислений (5 ступеней). Основные отличия: наличие встроенного математического сопроцессора (модели DX, DX2, DX4); поддержка многопроцессорного режима работы; два вида кэш-памяти – внутренней 8 Кбайт (L1) и внешней (L2). Начиная с процессора i80486, все последующие модели процессоров Intel поддерживают различные концепции энергосбережения. Интересно, что совершенствование i80486 шло в ходе его промышленного выпуска. Вследствие этого по своим возможностям  следующие  по времени выпуска процессоры i80486 отличались от предыдущих.</p>
<p><strong>Пятое поколение процессоров.</strong> Первый Pentium 60 (66), знаменитый своей ошибкой блока с плавающей точкой, был представлен в начале 1993 г. К внутреннему кэшу команд добавили 8 Кбайт для данных. Разработана суперскалярная архитектура (с двумя конвейерами u и v) – выполнение двух команд за один такт. Реализована технология предсказания переходов (branch prediction). Внутренние шины стали 128 и 256 бит, внешняя шина данных 64 бит.</p>
<p><strong>Шестое поколение процессоров.</strong> Линейку процессоров Pentium 75-200 МГц можно охарактеризовать по следующим особенностям: кэш L1 16 Кбайт на кристалле процессора; кэш L2 256/512 Кбайт внешний на материнской плате; технология изготовления 0,35 микрон (для процессоров 120 МГц и ниже 0,6 микрон); содержит около 3,3 миллиона транзисторов.</p>
<p>В это время помимо Intel, можно отметить еще двух производителей процессоров это Cyrix и AMD, которые совместно с IBM разрабатывают стандарт "Р-рейтинг" для обозначения производительности процессора. "Р-рейтинг" любого процессора равен величине тактовой частоты процессора Intel Pentium, показавший такой же или более высокий результат в абсолютно идентичной конфигурации (замеры производились при на тесте Winstone 96). Конечно, кроме рейтинга, эти две корпорации выпустили еще и процессоры, которые по соотношению цена/возможности превосходили процессоры Intel.</p>
<p>AMD выпускает процессор К5-PR133 (реально работающий на частоте 116,7 МГц). Этот процессор имеет встроенный кэш 24 Кбайт, технология изготовления 0,35 микрон, около 4,3 миллионов транзисторов. Процессоры CYRIX (и идентичные им с лейблом IBM) имеют официальные названия 6х86 Р120+, 6х86 Р133+, 6х86 Р150+, 6х86 Р166+, 6х86 Р200+. Откуда "+"? Дело в том, что при выполнении 32-разрядных тестов процессоры К5 и 6х86 показывают примерно на 11% большую производительность на соответствующем процессоре Pentium. Особенности 6х86: кэш 16 Кбайт, дополнительный кэш для команд 256 б; технология изготовления 0,5 микрон (0,65 для Р120+); количество транзисторов около 3 млн.</p>
<p><strong>Седьмое поколение процессоров.</strong> В конце 1995 г. Intel выпускает Pentium Pro, который до начала 1997 г. остается самым мощным (быстрее 8088 в несколько тысяч раз) и дорогим процессором. С этого процессора начинается архитектура Р6. Он выпускался с тактовыми частотами 150-200 МГц, имеет встроенный кэш первого уровня 16 Кбайт, второго 256/512 Кб (на кристалле процессора), технология изготовления 0,35 микрон, внутренняя шина 300 бит, около 5,5 млн. транзисторов. Высокая стоимость самого процессора и системной платы под него, высокое энергопотребление, а также заметный прирост производительности только под 32-разрядними операционными системами (Windows NT, OS/2) делают нецелесообразным использование Pentium Pro в компьютерах массового спроса, он находит свое применение в серверах и рабочих станциях.</p>
<p>Начиная с модели Pentium 133, был введен блок ММХ-команд (MultiMedia eXtensions). Цель данного блока увеличить производительность приложений по обработке звука, изображений, архивирования и др. Работа по обработке изображений на процессорах с ММХ выполнялась на 50% быстрее (если приложение не оптимизировано под ММХ, то на 7-11%). Кроме блока ММХ-команд, изменился еще и размер кэш-памяти до 32 Кбайт. Процессоры Pentium ММХ выпускались с рабочими частотами 133-233 МГц.</p>
<p>1997 г. - процессоры Pentium ММХ снимаются с производства, а в качестве альтернативы Intel выпускает Pentium II и Celeron. Если считать Pentium ММХ – обычным Pentium + ММХ, Pentium II – это усовершенствованный Pentium Pro с поддержкой ММХ. В этом процессоре удвоен объем кэш-памяти 16 Кбайт – для данных, 16 Кбайт – для команд. Кэш второго уровня выполнен не на кристалле (с целью удешевить процессор) и не на материнской плате (заметное снижение быстродействия). Был разработан новый разъем для процессора Slot 1 и сам процессор теперь представлял собой не отдельную микросхему, а картридж, внутри которого находился процессор и кэш второго уровня 512 Кбайт. При этом частота работы кэш-памяти второго уровня была в 2 раза ниже частоты процессора. Частота системной шины первых Pentium II была 66 МГц, а сами процессоры при этом работали на частотах 233-333 МГц. Позже Intel выпускает модификации Pentium II для частоты системной шины 100 МГц (модельный ряд 350, 400 МГц).</p>
<p>На рынок серверов выходят процессоры Xeon, отличающиеся от Pentium II размером кэша второго уровня 512/1024/2048 Кбайт (соответственно и ценой), разъем Slot 2. Он также обеспечивал поддержку многопроцессорности (до 8 процессоров, работающих одновременно).</p>
<p>Для дешевых настольных компьютеров выходит модификация Pentium II под названием Celeron (кодовое название ядра Covington). Первые два процессора Celeron 266 и 300 МГц отличались от Pentium II отсутствием внешнего контейнера и кэша второго уровня. Последующая модель выходит с индексом "А", что говорит о наличии кэша второго уровня 128 Кбайт, в исполнении Slot 1. Что бы еще больше удешевить Celeron, корпорация Intel выпускает линейку процессоров, начиная от Celeron 300А заканчивая Celeron 500 (все содержат кэш второго уровня). На рынке присутствуют 2 идентичных набора процессоров, отличающиеся только разъемом Slot 1 (картридж) и Socket 370 (микросхема с 370 ножками). Еще маленькая деталь из жизни Celeron-ов, они работают на частоте системной шины 66 МГц. Celeron 300А можно использовать на частоте 100 МГц (небольшой разгон, собственно это Pentium II только с урезанной памятью), при таком изменении он "превращается" в Celeron 450А и работает (в некоторых приложениях) быстрее Pentium II 450, !!! т. к. кэш второго уровня, хоть и обрезанный, но работает на частоте ядра процессора (у Pentium II в 2 раза ниже).</p>
<p>Один из соперников Pentium II был процессор Cyrix 6x86Mx (M2). Это переработанный 6x86, с улучшенной архитектурой (на 150-200% быстрее простого 6х86 той же частоты), также были добавлены некоторые команды к ММХ, дающие в итоге большой выигрыш в скорости (если приложение оптимизировано под эти процессоры!), например, множественное накопление (используется в 3D графике) выполнялось за два такта, вместо 3 - у Intel. Первые версии работали на частотах 180, 200 МГц, потом добавились 225 и 233МГц (225 работал быстрее 233!!!, т.к. использовал системную шину 75 МГц). Процессор имел встроенный кэш первого уровня 64 Кб (более рационально использовался), технология 0,25 микрон.</p>
<p>Корпорация AMD выпускает процессор К6, который не может похвастаться более высокой производительностью, но значительно дешевле процессоров от Intel, к тому же остался на старом разъеме Socket 7 (у Intel уже есть Socket 7, Socket 370, Slot 1, Slot 2). Размер кэш первого уровня 32 Кб данные, 32 Кб команды, технология 0,35, тактовая частота 166-233. К6 позиционируется как прямой конкурент Celeron. Соперник Pentium II в борьбе за покупателя был К6-2 (К6-3D), который содержит ряд команд для оптимизации работы с трехмерной графикой и звуком (3DNow!, оптимизация была включена в DirectX6, после успеха игры Quake2, разработчики видеокарт оптимизировали свои продукты под 3DNow!), устанавливается в Socket 7. Рабочие частоты 266-475 МГц.</p>
<p>В конце февраля 1999 г. были анонсированы Pentium III 450 и 500 МГц. Изготовлены по технологическому процессу 0,25 мкм, ядро Katmai, добавлен набор инструкций SSE, размер L1 кэш – 32 Кб (16+16), L2 кэш – 512 Кб (работает на половине частоты ядра, расположен рядом с микросхемой процессора в картридже), многопроцессорность (2 процессора на одной шине), идентификационный 96-битный номер – уникальный для каждого процессора (это вроде как преимущество, в криптографии и генерации случайных чисел, с другой стороны, о конфиденциальности можно забыть ;-) ). Фактически запуск этих двух процессоров выглядел, скорее, как маркетинговый прием Intel в борьбе с активно продвигающимся AMD К6-2.</p>
<p>В первой половине 1999 г. AMD начала поставки процессоров K6-III (К6-3D+), работающих в Socket 7. Главная особенность – встроенная кэш-память второго уровня 256 Кб (L1 кэш остался 64 Кб, что вдвое больше, чем Pentium III), работающая на полной частоте ядра (Pentium III – на половине частоты ядра), а кэш-память, установленная на материнской плате, рассматривается как кэш третьего (!!!) уровня. Тактовые частоты 400-500 МГц.</p>
<p>Долго оставаться в Socket 7 процессоры от AMD не могли (AMD выжала Socket 7 на все 200% :-) ). 23 июня 1999 г. лидерство в выпуске новых мощных процессоров перешло от Intel к AMD (хотя очень не надолго) – были представлены модели AMD Athlon 500, 550, 600, изготовленные по 0,25 микронной технологии в новом корпусе Slot А (чуть более тонкий картридж по сравнению с Slot 1).</p>
