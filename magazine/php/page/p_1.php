</I><B><I>m</I></B><I> и </I><B><I>n</I></B><I>, а также оси 
</I><B><I>e</I></B><I>, выбираемой по правилу</I> 
<B><I>e</I></B>&nbsp;=&nbsp;<B><I>m</I></B>&nbsp;&#215;&nbsp;<B><I>n</I></B>&nbsp;/&nbsp;|<B><I>m</I></B>&nbsp;&#215;&nbsp;<B><I>n</I></B>|<I>,</I></P>
