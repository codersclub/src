<H2>Вектор поворота</H2>
<P>Тензор поворота, записанный в форме 
<B><I>P&nbsp;</I></B>=&nbsp;<B><I>mm&nbsp;</I></B>+&nbsp;cos&#952;(<B><I>1</I></B><B><I>&nbsp;</I></B>–&nbsp;<B><I>mm</I></B>)&nbsp;+&nbsp;sin&#952;<B><I>m&nbsp;</I></B>&#215;&nbsp;<B><I>1</I></B>, 
не имеет каких-либо ограничений и может применяться всегда. Тем не менее, 
интуитивным образом поворота является не тензор второго ранга, а некий вектор 
<B><I>&#952;</I></B>, называемый вектором поворота</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>&#952;</I></B> = &#952;<B><I>m</I></B>, | 
      <B><I>m</I></B> | = 1. </TD>
    <TD align=right width="5%">(51) </TD></TR></TBODY></TABLE>
<P>Его направление совпадает с осью поворота <B><I>m</I></B>, а его модуль равен 
модулю угла поворота &#952;. В правоориентированной системе отсчета 
&#952;&nbsp;&gt;&nbsp;0 при взгляде с конца <B><I>m</I></B>, если поворот происходит 
против часовой стрелки. Поскольку поворот выражает реальное явление, то наличие 
нескольких способов его описания возможно только при условии, что способы 
связаны взаимно однозначным образом. Проще всего показать, что тензор поворота 
однозначно может быть вычислен по вектору поворота, что следует непосредственно 
из представления тензора поворота доставляемого теоремой Эйлера </P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> = [(1– cos&#952; 
      )/&#952;<SUP>2</SUP>]<B><I>&#952;&#952;</I></B> + cos&#952;<B><I>1</I></B> + 
      [sin&#952;/&#952;]<B><I>&#952;</I></B> &#215; <B><I>1</I></B>. </TD>
    <TD align=right width="5%">(52) </TD></TR></TBODY></TABLE>
<P>Видно, что последнее выражение нечувствительно к знаку &#952;. Поэтому величину &#952; 
в (52) можно рассматривать как модуль вектора <B><I>&#952;</I></B>, что позволяет без 
дополнительных соглашений однозначно вычислять тензор поворота по вектору 
поворота. В теории поворотов доказывается теорема, что представление (52) 
является ничем иным как тензорной экспонентой тензора 
<B><I>&#952;</I></B><B><I>&nbsp;</I></B>&#215;&nbsp;<B><I>1</I></B></P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>P</I></B> = exp(<B><I>&#952;</I></B> &#215; 
      <B><I>1</I></B>). </TD>
    <TD align=right width="5%">(53) </TD></TR></TBODY></TABLE>
<P>Вам это ничем не напоминает комплексную экспоненту exp(&#952;<I>i</I>), которая 
является оператором поворота векторов на комплексной плоскости вокруг оси 
перпендикулярной к ней? Общность явлений порождает общность описаний, правда со 
своими особенностями.</P>
<P>Не будем приводить здесь формулы для вычисления вектора поворота по тензору 
поворота, скажем лишь, что это можно также однозначно сделать. В заключение 
этого пункта приведем формулу для повернутого вектора <B><I>a</I></B>&#8242; </P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>a</I></B>&#8242; <SMALL>&#8801;</SMALL> 
      <B><I>P</I></B>(<B><I>&#952;</I></B>) · <B><I>a</I></B> = <B><I>a</I></B> + 
      [sin&#952;/&#952;]<B><I>&#952;</I></B> &#215; <B><I>a</I></B> + [(1– cos&#952; 
      )/&#952;<SUP>2</SUP>]<B><I>&#952;</I></B> &#215; (<B><I>&#952;</I></B> &#215; <B><I>a</I></B>), </TD>
    <TD align=right width="5%">(54) </TD></TR></TBODY></TABLE>
