      <B><I>P</I></B><SUB>2</SUB>·<B><I>P</I></B><SUB>1</SUB> · <B><I>a</I></B> 
      = <B><I>P</I></B> · <B><I>a</I></B> ,&nbsp; <B><I>P</I></B> 
      <B><I>=</I></B> <B><I>P</I></B><SUB>2</SUB>·<B><I>P</I></B><SUB>1</SUB>. 
    </TD>
