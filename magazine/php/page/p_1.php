<H2>Тензоры второго ранга</H2>
<P>Тензоры нулевого ранга (скаляры) и тензоры первого ранга (векторы) не 
исчерпывают списка необходимых для физики и, в частности, механики понятий. 
Необходимы и более сложные конструкции – тензоры высших рангов. На интуитивном 
уровне тензоры ощущаются уже не столь отчетливо, как скаляры и векторы. Поэтому 
требуется определенная тренировка на уровне логического мышления, прежде чем 
исчезнет неудобство, связанное с восприятием тензора второго ранга, как чисто 
формального объекта.</P>
<P>Для целей данной статьи нам необходимо научиться работать с парами векторов, 
рассматриваемых как единое целое и называемых диадами. Упорядоченную пару 
векторов <B><I>ab</I></B> будем называть диадой. Термин «упорядоченность» 
означает, что порядок следования векторов в диаде важен</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>ab</I></B> <SMALL>&#8800;</SMALL> 
      <B><I>ba</I></B>. </TD>
    <TD width="5%">&nbsp; </TD></TR></TBODY></TABLE>
<P>Точно также как и для векторов, для того, чтобы множество диад представляло 
реальный интерес нужно ввести на этом множестве законы композиции элементов 
(операции над элементами). Прежде всего, введем операции произведения диады на 
вещественное число и операцию суммы диад. </P>
<P>Произведением диады <B><I>ab</I></B> на вещественное число &#945; называется 
диада, вычисляемая по правилу</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">&#945;(<B><I>ab</I></B>) = (&#945; 
      <B><I>a</I></B><B>)<I>b</I></B> = <B><I>a</I></B>(&#945;<B><I>b</I></B>) = 
      &#945;<B><I>ab</I></B> . </TD>
    <TD align=right width="11%">(4) </TD></TR></TBODY></TABLE>
<P><B><I>Аксиома</I></B>:</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">(&#945; + &#946;)<B><I>ab</I></B> = &#945;<B><I>ab</I></B> + 
      &#946;<B><I>ab</I></B>. </TD>
    <TD align=right width="11%">(5) </TD></TR></TBODY></TABLE>
<P>Из (4) при &#945; = 0 имеем </P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">0<B><I>ab</I></B> = <B><I>0b</I></B> = 
      <B><I>a0</I></B>. 
      <P></P></TD>
    <TD width="5%">&nbsp; </TD></TR></TBODY></TABLE>
<P>Из (5) при &#945; = 1 и &#946; = 0 имеем </P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>ab</I></B> = (1+0)<B><I>ab</I></B> = 
      1<B><I>ab</I></B> + 0<B><I>ab</I></B> = <B><I>ab</I></B>. </TD>
    <TD width="5%">&nbsp; </TD></TR></TBODY></TABLE>
<P>Поэтому диаду 
0<B><I>ab</I></B><B><I>&nbsp;</I></B>=&nbsp;<B><I>0b</I></B><B><I>&nbsp;</I></B>=&nbsp;<B><I>a0</I></B> 
называют нулевой диадой: ее добавление к произвольной диаде <B><I>ab</I></B> не 
меняет диады <B><I>ab</I></B>.</P>
<P>Далее примем правила сложения диад с одинаковыми правыми (левыми) 
сомножителями</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>a</I></B> (<B><I>b</I></B> + 
      <B><I>c</I></B>) = <B><I>ab</I></B> + <B><I>ac</I></B> , 
      <BR>(<B><I>a</I></B> + <B><I>b</I></B>)<B><I>c</I></B> = <B><I>ac</I></B> 
      + <B><I>bc</I></B>. </TD>
    <TD align=right width="5%">(6) </TD></TR></TBODY></TABLE>
<P>Но что, к примеру, означает «сумма»</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>ab</I></B> + <B><I>cd</I></B> ? </TD>
    <TD width="5%">&nbsp; </TD></TR></TBODY></TABLE>
<P>Оказывается в множестве диад данную операцию не определить. Необходимо 
расширить множество. Будем рассматривать множество неупорядоченных совокупностей 
<B><I>трех</I></B> упорядоченных пар векторов. Во сказанул!!! Не имея 
возможности объяснить более подробно, скажем, что именно на множестве таких 
элементов можно определить операцию суммы, не выводящую за пределы множества. 
Также можно показать, что сумма (неупорядоченная совокупность) конечного числа 
упорядоченных пар векторов (диад) сводится к совокупности не более чем трех 
диад.</P>
<P><B><I>Определение:</I></B> Тензором второго ранга в трехмерном пространстве 
называется неупорядоченная совокупность конечного числа упорядоченных пар 
векторов (диад)</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%"><B><I>A</I></B> = <B><I>ab</I></B> + 
      <B><I>cd</I></B> + ... + <B><I>mn</I></B>. </TD>
    <TD align=right width="5%">(7) </TD></TR></TBODY></TABLE>
<P>«Неупорядоченная совокупность» означает, что порядок слагаемых может быть 
произвольным. Заметим, что стремиться свести (7) к совокупности трех диад не 
только не нужно, но и, как правило, это нецелесообразно.</P>
