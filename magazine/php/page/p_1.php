следующая. Матрицы поворота являются лишь маскировкой, которая ничего, кроме 
неясности в трактовки не вносит. Попробуйте, например, попросить человека 
написать без шпаргалки матрицу поворота вокруг произвольной оси на произвольный 
угол. Тем, кому это под силу, нет нужды читать эту статью. Те же, кто прочитает 
эту и следующую нашу статью, научатся это делать в течение нескольких минут. 
</P>
<P>Пусть читатель не удивляется и простит автора за то, что статья написана от 
третьего лица, так как будто бы автор весь излагаемый материал сам придумал. 
Отнюдь нет. В хороших книгах, правда их немного, можно найти гораздо более 
качественное изложение. Просто автор считает, что статья, написанная в безличной 
форме, может отпугнуть впервые изучающего данный предмет, т.к. бесконечные фразы 
типа «в математике рассматривается…» и т.п. оставляют у читателя чувство 
неуверенности в полученных знаниях и необходимости обращения к дополнительной 
литературе, в которой подчас нет никакой надобности.</P>
<H2>Из истории…</H2>
<P>Развитие всей рациональной науки на протяжении столетий необходимо 
сопровождалось развитием, не всегда осознанным, тензорного языка, процесс 
становления которого был очень длительным и трудным. Например, понятие вектора 
было введено Саймоном Стевином (S. Stevin) в 1580 году, но первые учебники по 
механике, использующие векторный <B>язык</B>, появились только в начале XX века. 
</P>
<P>Тензоры второго ранга (тензор поворота, тензор инерции твердого тела) впервые 
были введены Леонардом Эйлером (Leonhard Euler) в 1758 году. Термин «тензор», 
однако, был введен лишь в 1900 году по предложению В. Фойгта (W. Voigt). Тем не 
менее, Л. Эйлер работал с этими объектами как с тензорами. Важно подчеркнуть, 
что Л. Эйлер на формальном уровне не владел многими стандартными ныне методами. 
Однако, по числу новых фундаментальных результатов ему нет равных в истории 
человечества, за исключением разве что Архимеда. В огромной степени это 
объясняется тем, что Л. Эйлер обладал совершенно феноменальной интуицией, т.е. 
способностью непосредственного визуального восприятия образов вводимых им 
объектов.</P>
<H2>Векторы в трехмерном пространстве</H2>
<P>В физике используется огромное множество различных понятий. Это неизбежно 
приводит к необходимости их классификации. Наиболее общей является классификация 
по так называемому тензорному признаку, так как все понятия, описывающие 
количественные характеристики физических объектов, являются тензорами, но 
тензорами различных рангов. Тензоры нулевого ранга называются скалярами. Тензоры 
первого ранга называются векторами. Эти два типа тензоров хорошо известны из 
школьных курсов математики и физики. Тем не менее, кратко напомним их 
определения и основные операции над ними.</P>
<P><B><I>Определение:</I></B><I> физические величины, которые полностью 
характеризуются заданием одного вещественного числа, не зависящего от выбора 
системы координат, называются скалярами.</I></P>
<P>Примерами скаляров являются масса, объем, температура. Координаты вектора, 
однако, нельзя назвать скалярами, т.к. координаты вектора меняются при замене 
системы координат (смене базиса). </P>
<P>Следующей после скаляров ступенькой в классификации физических величин 
являются векторы или тензоры первого ранга. Векторы являются не столько 
математическими изобретениями, сколько элементами нашего интуитивного мышления. 
</P>
<P>В Природе существуют, по крайней мере, два принципиально несводимых друг к 
другу вида движения тел. Первый тип движения характеризует перенос (трансляцию) 
тела в пространстве. Второй тип характеризует изменение ориентации тела в 
пространстве. Оба типа движения интуитивно понятны человеку с рождения. Одной из 
причин такой понятности, возможно, является то, что человек начиная с 
эмбрионального состояния, вынужден совершать оба типа движений. </P>
<P>Соответственно двум типам движения существуют две формальные логические 
конструкции отображения объективной реальности. Это направленный отрезок (прямой 
вектор) и спин-вектор. </P>
<P>Два примера. Первый. Любой нормальный человек, например, гуляя по парку и 
увидев стрелку-указатель с надписью «Кафе 100 м» поймет, что нужно делать. 
Второй. Предложите группе людей, не математиков и не физиков, например, 
филологов, схематически изобразить на листке бумаги движение обычного 
вращающегося волчка. В «сухом остатке» у большинства картина будет примерно 
такой, как на рис. 1.</P>
<P align=center><IMG height=134 src="'. $site['setting']['base'] .'/img/m/0804/3d_rot/image001.jpg" width=86 
border=0></P>
<P align=center>Рис. 1. Спин-вектор</P>
<P>Подведем итог. Чтобы задать направленный отрезок (прямой вектор), необходимо 
указать в физическом трехмерном пространстве (системе отсчета) направление и 
вещественное число (скаляр), называемое длиной (модулем) вектора. Чтобы задать 
спин-вектор, необходимо в физическом трехмерном пространстве задать прямую, 
называемую осью спин-вектора, затем указать направление вращения и поставить в 
соответствие данной конструкции вещественное число, называемое длиной (модулем) 
спин вектора. Данное число может характеризовать не только угол поворота, но и 
скорость поворота, и т.д.</P>
<P>Однако работать с двумя объектами различной природы неудобно. Тем более, что 
спин-векторам можно взаимно однозначно сопоставить прямые векторы, введя 
дополнительное соглашение, называемое ориентацией системы отсчета.</P>
<P>Пусть дан спин-вектор <B><I>a</I></B><SUB>*</SUB>. Сопоставим ему прямой 
вектор <B><I>a</I></B> по следующим правилам:</P>
<P style="MARGIN-LEFT: 1cm">а) вектор <B><I>a</I></B> расположен на оси 
спин-вектора <B><I>a</I></B><SUB>*</SUB>; </P>
<P style="MARGIN-LEFT: 1cm">б) модуль <B><I>a</I></B> равен модулю 
<B><I>a</I></B><SUB>*</SUB>;</P>
<P style="MARGIN-LEFT: 1cm">в) вектор <B><I>a</I></B> направлен так, чтобы при 
взгляде с его конца круговая стрелка показывала вращение против хода часовой 
стрелки;</P>
<P>либо</P>
<P style="MARGIN-LEFT: 1cm">г) вектор <B><I>a</I></B> направлен так, чтобы при 
взгляде с его конца круговая стрелка показывала вращение по ходу часовой 
стрелки.</P>
<P>Выбор одной из двух возможностей называется ориентацией системы отсчета. 
Важно осознать, что Природа ничего не знает о нашем произволе. Поэтому в 
используемых нами математических конструкциях для описания объективной 
реальности должны существовать средства компенсации данного произвола. Как мы 
увидим, основной объект обсуждения&nbsp; данной статьи&nbsp;– тензор 
поворота&nbsp;– не зависит от ориентации системы отсчета. </P>
<P><B><I>Определение:</I></B><I> система отсчета называется называется 
правоориентированной, если трансляции и вращения в ней согласованы в 
соответствии&nbsp; с правилами а), б) и в). Система отсчета называется 
левоориентированной, если трансляции и вращения в ней согласованы в соответствии 
с правилами а), б) и г).</I></P>
<P>Таким образом, в ориентированной системе отсчета мы можем работать только с 
одним множеством объектов: множеством направленных отрезков. Однако в этом 
множестве все равно сохраняется различие между истинно прямыми и псевдопрямыми 
векторами, прообразами которых служат спин-векторы.</P>
<P>Для первого знакомства достаточно сказанных весьма общих слов про прямые и 
псевдопрямые векторы. Чтобы не уходить в сторону от главной задачи, оставим пока 
эту тему. Глубинное понимание различия понадобится лишь при введении понятия 
угловой скорости, что, возможно, будет сделано в одной из наших следующих 
статей.</P>
