немецкоязычной литературе) называется число, вычисляемое по правилу</P>
<TABLE cellSpacing=0 cellPadding=0 width="100%" border=0>
  <TBODY>
  <TR>
    <TD align=middle width="95%">tr <B><I>A</I></B> = Sp <B><I>A</I></B> = 
      <B><I>a</I></B> · <B><I>b</I></B> + … + <B><I>c</I></B> · <B><I>d</I></B>. 
    </TD>
    <TD align=right width="5%">(20) </TD></TR></TBODY></TABLE>
<P>Справедливы тождества 
tr<B><I>A&nbsp;</I></B>=&nbsp;tr<B><I>A</I></B><I><SUP>T</SUP></I>, 
tr(<B><I>A</I></B>·<B><I>B</I></B>)&nbsp;=&nbsp;tr(<B><I>B</I></B>·<B><I>A</I></B>)&nbsp;=&nbsp;<B><I>A</I></B>··<B><I>B</I></B>, 
tr(<B><I>A</I></B>·<B><I>B</I></B>)&nbsp;=&nbsp;tr(<B><I>A</I></B><I><SUP>T</SUP></I>·<B><I>B</I></B><I><SUP>T</SUP></I>).</P>
