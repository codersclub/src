<h3>array [0..n] of Char</h3>
<p>Формально, этот тип не являются строковыми. Однако, в Delphi, он несколько отличаются от остальных типов массивов. А именно, если массив символов имеет нижнюю границу индекса равной 0, то Delphi считает такой тип совместимым по присваиванию со строковыми константами. Например, если переменная a будет описана как a :array[0..20] of Char, то оператор a := \'abc\' будет допустим. Причём, значения элементов начиная с a[3] и до  a[20] будут установлены в #0.</p>
<p>Больше ничего необычного в этом типе нет. Хотя забыл, есть ещё – оператор @ (получение указателя) для переменной такого типа возвращает значение типа pChar. Это очень удобно, поскольку переменные этого типа очень часто используются как буфер при работе с функциями Windows API. Например:</p>
<pre class=code>
var
  a :array[0..20] of Char;
...
  GetModuleFileName(GetModuleFileName(HInstance,@a,SizeOf(a));
</pre>
<p>Здесь, функция GetModuleFileName возвращает результат в массив a.</p>

<h3>pChar</h3>
<p>Этот тип широко используется в языках C и C++. В Delphi, это не фундаментальный тип, а производный. Его определение выглядит так:</p>
<pre class=code>
pChar = ^Char
</pre>
<p>Т.е. переменные этого типа являются указателем, поэтому и имеют размер 4 байта. Формально, значение pChar может указывать как на один символ, так и на строку символов. Однако, общепринято что значения pChar указывают на строки, завершающиеся символом с кодом 0 (#0).  В DOSе, такие строки назывались ASCIIZ, но чаще можно встретить название null-terminated string. Наличие такого «концевика» позволяет легко определить реальный размер строки на которую указывает значение pChar.</p>
<p>Не смотря на то, что формально pChar это указатель на Char (^Char), как это часто бывает в  Delphi, тип pChar имеет несколько особенностей по сравнению с другими указателями. Таких особенностей несколько.</p>

<p>Первая, заключается в том что константы, и глобальные переменные этого типа могут быть проинициализированы строковой константой. Вот пример:</p>
<pre class=code>
const pc :pChar =\'abc\';
var   pv :pChar =\'abc\';
</pre>
<p>Эти строки, определяют константу pc и переменную pv типа pChar. При этом, и pc и pv указывают на разные области памяти, но содержащие одинаковые значения, состоящие из трех символов: \'a\', \'b\', \'c\', и символа #0. Замечу, что завершающий символ с кодом 0 компилятор добавил автоматически.</p>
<p>Вторая особенность в том, что к переменным типа pChar применимо обращение как к массиву символов. Например, если есть приведенные выше определения, тогда:</p>
<pre class=code>
C := pv^;   // C будет присвоен символ \'a\'. Это обычное обращение
C := pv[0]; // необычно, но С станет равным \'a\'
C := pv[1]; // С станет равным \'b\'
C := pv[2]; // С станет равным \'c\'
C := pv[3]; // С станет равным #0
C := pv[4]; // ОШИБКА!
</pre>
<p>Замечу, символ с индексом 3 отсутствует в строке, однако, там есть завершающий ее символ с кодом 0. Именно он будет результатом pv[3]. О pv[4] тоже стоит сказать особо. Дело в том, что компилятор не даст ошибки при компиляции, поскольку на этапе компиляции он, в общем случае, не известен реальный размер строки, на которую указывает переменная pv. Однако, на этапе выполнения программы, такое обращение может вызвать ошибку нарушения доступа к памяти (Access Violation). А может и не вызвать, но результатом будет неопределённое значение. Все зависит от «расклада» в памяти. Поэтому, при таком способе обращения необходимо быть внимательным, и выполнять все необходимые проверки, исключающие выход за размеры строки.</p>
<p>Третья и последняя особенность типа pChar в том, что к значениям этого типа применима так называемая адресная арифметика. Тем, кто программирует на C и C++ она хорошо знакома. Суть её в том, что значения pChar можно увеличивать, уменьшать, вычитать, и складывать. Для демонстрации использования этой особенности, приведу пример реализации функции подсчитывающей длину строки, указатель на которую передается в качестве параметра.</p>
<pre class=code>
function StringLength (p :pChar) :Cardinal;
begin
  Result := 0;
  if  p = nil  then Exit;
  while p^ <> #0 do begin
    Inc(Result);
    Inc(p);
  end;
end;
</pre>
<p>Здесь важно обратить внимание на два нюанса.</p>
<p>Первый, это проверка переданного указателя на nil. Такая проверка необходима, поскольку очень часто, для обозначения пустой строки используется значение nil.</p>
<p>Второй, это оператор Inc(p) – он «продвигает» указатель на следующий символ. Можно было бы записать его и так: p := p + 1.</p>
<p>Что бы продемонстрировать вычитание указателей pChar, я приведу еще один вариант реализации той же функции:</p>
<pre class=code>
function StringLength (p :pChar) :Cardinal;
var pp :pChar;
begin
  Result := 0;
  pp := p;
  if  pp <> nil  then
    while pp^ <> #0 do
      Inc(pp);
  Result := (pp-p);
end;
</pre>
<p>Здесь, выражение pp-p дает «расстояние» между указателями, т.е. число символов между символом, на который указывает указатель p (начало строки) и символом, на который указывает указатель pp (завершающий строку #0).</p>
<p>На этом, мы пока закончим рассмотрение типа pChar, но, он еще встретится, когда мы будем рассматривать вопросы преобразования строковых типов.</p>

<h3>ShortString, и String[n]</h3>
<p>ShortString является частным случаем String[n], а если быть более точным, он полностью эквивалентен String[255].</p>
<p>Если посмотреть на таблицу, где приведены характеристики переменных этого типа, то мы увидим что их размер на один байт больше чем размер хранимой в них строки. Но в данном случае, это не для завершающего символа, как в pChar. Здесь в дополнительном байте хранится текущий размер строки. Кроме того, этот байт располагается не в конце строки, а наоборот, в ее начале. К примеру, если имеется следующее определение:</p>
<pre class=code>
var s :String[4];
</pre>
<p>Оно означает, что для переменной s будет статически выделена область памяти размером 5 байт.</p>
<p>Теперь, выполнение оператора s := \'abc\', приведёт к тому, что содержимое этих пяти байт станет следующим: байт 1 = 3, байт 2 = \'a\', байт 3 = \'b\', байт 4 = \'c\', а значение байта 5 будет неопределённо – оно будет зависеть от «расклада»  в памяти. Т.е., первый символ строки будет находиться во втором байте. Это неудобно, поэтому к символам строк ShortString принято индексироваться, начиная с 1. Следовательно:</p>
<pre class=code>
s[1] = \'a\' – первый символ строки
s[2] = \'b\' – второй символ строки
s[3] = \'c\' – третий символ строки
s[4] = все что угодно :).
</pre>
<p>А как же байт длины? Да все очень просто, к нему можно обратиться как s[0]. Только вот есть маленькая проблемка. Поскольку элементами строки являются символы, то и тип значения s[0] тоже будет символ. Т.е., если Вы хотите получить длину строки в виде целого числа, как это принято у нормальных людей, то надо выполнить соответствующее преобразование типа: Ord(s[0]) = 3 – размер строки.</p>
<p>Теперь, я думаю, Вам станет ясно, почему для типа String[n] существует ограничение 0<=n<=255. Это весь набор значений, которые могут быть представлены в одном байте. На всякий случай, отмечу, что размерность переменной (n) определяет размер выделяемой под эту переменную памяти, и он не зависит от размера строки, которая там хранится. Т.е., если, например переменная описана как String[30], то даже если присвоить ей значение \'abcd\', то размер этой переменной все равно останется 31 байт. Это иногда бывает очень удобно, например, при записи (чтении) таких переменных в файл (из файла).</p>
<p>В качестве примера работы со структурой хранения такого типа, приведу пример все той же функции возвращающей длину строки:</p>
<pre class=code>
function StringLength (s :ShortString) :Cardinal;
begin
  Result := Ord(s[0]);
end;
</pre>
<p>Как видите, это код существенно компактнее, и быстрее чем соответствующий код для pChar. Именно поэтому, он и применялся в языке Pascal.</p>
<p>Однако ясно, что ограничения переменных этого типа – максимальный размер строки 255 символов, и всегда максимальный размер занимаемой переменной памяти, вне зависимости от реально помещенной в нее строки, заставили разработчиков Delphi вести новые строковые типы. Вот к ним мы сейчас и перейдем.</p>

<h3>AnsiString и WideString</h3>
<p>Строки этого типа объединили в себе ряд качеств как строк ShortString и их байтом длины, так и строк завершающихся нулем (pChar). Последнее было необходимо, поскольку к моменту их появления, засилье C-ишников было уже так велико :), что большинство системных функций Windows API оперировало строками именно такого формата. А если серьезно, то такой формат строк хоть и более трудоемок в обработке, зато в принципе лишен ограниченности на максимальный размер строки. Ведь хранение размера строки всегда ограниченно какими-либо рамками: если хранить в байте, то ограничение 255 байт; если хранить в слове, то ограничение 65535 символов; и т.д. Однако, совсем отказываться от хранения текущей длинны строки в Borland не стали. Но об этом позже.</p>
<p>Напомню, что еще одним из недостатков статически размещаемых строк ShortString было "расточительство". Т.е. определив переменную такого типа, мы заранее резервировали под нее 256 байт памяти, поэтому, если мы один раз, во всей программе, присвоили ей значение \'abcd\', то 251 байт памяти мы просто «пустили на ветер». Казалось бы, а зачем так определили, написали бы String[4], и ничего не потеряли бы. Но, когда мы пишем программу, мы же чаще всего не знаем что мы будем «класть» в эту переменную. Вот и определяем с запасом. Решением этой проблемы стало использование динамически размещаемых строк.</p>

<h2>Так как они устроены?</h2>
<p>Если вы обратили внимание, в таблице характеристик строковых типов, размер переменных AnsiString равен четырем байтам, как и у pChar. Это говорит нам о том, что переменные этого типа тоже являются указателями. Но, в отличие от pChar, в данном случае, это скрыто реализацией. Т.е. программист, работает с ними как с обычными строками: не надо выделять под них память, не надо заботиться о наличии завершающего нуля, не надо волноваться об изменении размеров строки и т.п. Всю эту работу берет на себя компилятор Delphi. Более того, он ещё и занимается оптимизациями. В частности, если например выполнить следующий код:</p>
<pre class=code>
var s1, s2 :AnsiString;
...
s1 := \'abc\';
s2 := s1;
</pre>
<p>то в памяти будет храниться лишь ОДИН экземпляр строки \'abc\'! Во как.</p>

<h3>Как же это происходит?</h3>
<p>При выполнении первого оператора (присваивание s1), указатель, хранящийся в переменной s1 настраивается на область памяти в которой размещена строка \'abc\'. При выполнении второго оператора, в s2 попадает тот же адрес! Т.е. в памяти при это присутствует лишь один экземпляр строки \'abc\'. Да и зачем нам нужны дублирующие себя строки.</p>
<p>Не нужно программисту заботиться и об освобождении памяти занятой ненужными уже строками. Вот например есть такая процедура:</p>
<pre class=code>
procedure ShowInteger;
var 
  s :AnsiString;
  n :Integer;
begin
  n := 123;
  s := \'Значение переменной равно \'+IntToStr(n);
  ShowMessage(s);
end;
</pre>
<p>Здесь, при выполнении присваивания, Delphi создаст в памяти экземпляр строки \'Значение переменной равно 123\', и присвоит адрес этой строки переменной s. Однако, при завершении процедуры, переменная s перестанет существовать – она же локальная. Значит, и экземпляр строки тоже уже не нужен – на него некому будет указывать. Вот поэтому, Delphi автоматически освободит память, выделенную под строку, как только, выполнение процедуры достигнет строки end. Более того, даже если во время выполнения процедуры возникнет исключительная ситуация, при которой "хвост" процедуры может и не выполниться, Delphi всё равно корректно освободит память для всех строк, распределенных в этой процедуре. Достигается это неявным использованием механизма подобного try - finally.</p>
<p>Казалось бы, всё замечательно. Но попробуем усложнить ситуацию.</p>
<pre class=code>
var
  gs :AnsiString; // глобальная переменная
procedure ShowInteger;
var 
  s :AnsiString;
  n :Integer;
begin
  n := 123;
  s := \'Значение переменной равно \'+IntToStr(n);
  gs := s;
  ShowMessage(s);
end;
</pre>
<p>Теперь, к моменту завершения процедуры, на экземпляр строки \'Значение переменной равно 123\' уже ссылаются две переменные s и gs. И, несмотря на то, что область существования переменной s заканчивается, освобождать память, выделенную под строку на которую она указывает нельзя! Ведь позже, возможны обращения к переменной gs.</p>
<p>Для того, чтобы корректно обрабатывать такие ситуации, Delphi для каждой динамически распределенной строки ведет так называемый "счётчик ссылок". Т.е., как только он присваивает какой-либо из строковых (AnsiString) переменных ссылку на распределенную в памяти строку, то он увеличивает этот счетчик на единицу. Первоначально, при присваивании динамически распределённой строки, первой переменной (в примере s), значение этого счётчика устанавливается равным единице. В последствии, при прекращении жизни каждой строковой переменной, он уменьшает на 1 этот счетчик для той строки на которую она указывает. Если счётчик становится равным 0, то значит более нет строковых переменных, указывающих на данную строку. Значит, ее можно освобождать. Благодаря такому алгоритму, после присваивания в примере значения переменной gs, у строки \'Значение переменной равно 123\' счетчик ссылок становится равным 2. Следовательно, при "умирании" переменной s, он декрементируется, и становится равным 1. Т.е. >0, поэтому то Delphi и не освобождает память, занятую строкой.</p>
<p>Еще, этот счётчик используется и для разрешения проблем, связанных со следующей ситуацией:</p>
<pre class=code>
procedure ShowInteger;
var 
  s1 :AnsiString;
  s2 : AnsiString;
  n :Integer;
begin
  n := 123;
  s1 := \'abc\'+IntToStr(n);
  s2 := s1;
  s2[1] := \'X\';
end;
</pre>
<p>Здесь, как мы уже знаем, после выполнения оператора s2 := s1, обе переменные указывают на один и тот же экземпляр строки \'abc123\'. Однако, что же произойдёт когда выполниться оператор s2[1] := \'X\'? Казалось бы, в единственном имеющимся в нашем распоряжении экземпляре строки первая буква будет заменена на \'X\'. И как следствие, обе строки станут равными \'Xbc123\'. s1 то за что "страдает"? Но, к счастью это не так. Здесь на помощь Delphi вновь приходит счетчик ссылок. Delphi, при выполнении этого оператора понимает, что строка на которую указывает s2 будет изменена, а это может повлиять на других. Поэтому, перед изменением строки, проверяется ее счётчик ссылок. Обнаружив, что на нее ссылается более одной строковой переменной, делается следующее: создается копия этой строки со счётчиком ссылок равным 1, и адрес этой копии, присваивается s2; У исходного экземпляра строки, счетчик ссылок уменьшается на 1 – ведь s2 на неё теперь не ссылается.  И лишь после этого, происходит изменение первой буквы, теперь уже собственного экземпляра строки. Т.е., по окончанию выполнения этого оператора, в памяти будут находиться две строки: \'abc123\' и \'Xbc123\'. Причем, s1 будет ссылаться на первую, а s2 на вторую.</p>
<p>При работе со строками определенными как константы, алгоритм работы несколько отличается. Приведу пример:</p>
<pre class=code>
procedure ShowInteger;
var s :AnsiString;
begin
  s := \'Вася\';
  ShowMessage(s);
end;
</pre>
<p>Казалось бы, при завершении работы процедуры, экземпляр строки \'Вася\' должен быть уничтожен. Но в данном случае это не так. Ведь, при следующем входе в процедуру, для выполнения присваивания нужно будет вновь где-то взять строку \'Вася\'. Для этого, ещё при компиляции, Delphi размещает экземпляр строки \'Вася\' в области констант программы, где её даже невозможно изменить, по крайней мере, простыми методами. Но как же при завершении процедуры определить что строка \'Вася\' – константная строка, и ее нельзя уничтожать? Все очень просто. Для константных строк, счётчик ссылок устанавливается равным -1. Это значение, "выключает" нормальный алгоритм работы со "счётчиком ссылок". Он не увеличивается при присваивании, и не уменьшается при уничтожении переменной. Однако, при попытке изменения переменной (помните s2[1]:=\'X\'), значение счётчика равное -1 будет всегда считаться признаком того, что на строку ссылается более одной переменной (ведь он не равен 1). Поэтому, в такой ситуации всегда будет создаваться уникальный экземпляр строки, естественно, без декремента счётчика ссылок старой. Это защитит от изменений экземпляр строки-константы.</p>
<p>К сожалению, этот алгоритм срабатывает не всегда. Но об этом, мы поговорим позже, при рассмотрении вопросов преобразования строковых типов.</p>
