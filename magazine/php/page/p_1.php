<p>После этого AMD выпустила еще несколько процессоров с более высокой частотой. 29 ноября 1999 г. были выпущены процессоры Athlon с частотами от 550-800 МГц, изготовленные по технологии 0,18 мкм (что бы отличать они именовались Model 1 - 0,25мкм и Model 2 - 0,18 мкм). Основные характеристики: внутренняя архитектура – типа "RISC"; имеет 3 конвейера для целочисленных вычислений и 3 для операций с плавающей точкой; добавлены новые команды в блок 3DNow!, теперь носит название Enhanced 3DNow!; L1 кэш – 128 Кб (64+64), L2 кэш – 512 Кб (в перспективе до 8 Мб) расположен в отдельных микросхемах рядом с процессором и работает на половине частоты ядра, поддерживает ECC-механизм; многопроцессорность – теоретически до 14 процессоров на одной шине; системная шина – 100 МГц, но работает по обоим фронтам сигнала, результирующая 200 МГц.</p>
<p>"Настоящий" процессор Pentium III (ядро Coppermine). Intel 25 октября 1999 г. анонсировала сразу 9 (!) процессоров с частотами 500-733 (с индексами E, EB). В последствии к ним добавились еще Pentium III 750, 800, 800EB, 900 МГц и 1,14 ГГц. Характеристики новой линейки процессоров: выполнен в Slot 1 или Socket 370 (FC-PGA) технология 0,18 мкм; 28 млн. транзисторов; L1 кэш – 32 Кб (16+16), L2 кэш – 256 Кб (L2 – работает на частоте ядра и располагается на кристалле процессора), поддерживает ECC-механизм обнаружения и коррекции ошибок при обмене данными с ядром процессора; частота системной шины 100 или 133 МГц, поддерживает ECC.</p>
<p>Новое поколение Celeron (на ядре Coppermine) отличается от своего более дорогого собрата,  Pentium III, урезанным L2 кэш – 128 Кб. Этот процессор также поддерживает ММХ и SSE (нет поддержки 3DNow!). От более ранних версий Celeron отличается 256-битной внутренней шиной (между кэшем и ядром). Помимо этого избавились от злосчастного серийного номера (образованную общественность насторожила способность отслеживания действий пользователя). Технология изготовления 0,18 мкм (тут пришлось использовать другой разъем Socket 370 FC-PGA (отличается от Socket 370, разводкой пары ножек, напряжением питания и более жестким требованиям к питанию процессора).</p>
<p>Технология 0,18 мкм с архитектурой Р6 исчерпала себя полностью, поэтому для дальнейшего повышения частоты Pentium III, перешел на новые технологии 0,13 мкм. Новое ядро получило название Tualatin, в свет вышли новые модели Pentium III 1,13 и 1,2 ГГц (позже 1.3, 1.4, 1.5 ГГц). С выходом Tualatin, Intel, наконец, смогла осуществить свою давнюю мечту 512 Кб кэша на одном кристалле процессора и работающий на полной частоте процессора. Идя навстречу пользователей, Intel, удалил 96-битный идентификатор процессора. На ядре Tualatin, были выпущены также более легкие процессоры Celeron, мобильный вариант ядра Tualatin-М, серверный – Tualatin-S. В гонке за покупателем проект ядра Tualatin (так и не исчерпав своих ресурсов) был закрыт, корпорация Intel вместе с ним закрывает историю архитектуры Р6.</p>
<p>Переход на технологию 0,18 мкм для AMD состоялся летом 2000 г. разработкой ядра Thunderbird. Для своих новых процессоров Athlon AMD разрабатывает также новый разъем Socket А (Socket 462 в виде микросхемы). Под "капотом" нового процессора содержится 37 млн. транзисторов. L1 кэш – 128 Кб, L2 кэш – 256 Кб (L2 находится на кристалле процессора). Единственное "узкое" место (во всех смыслах этого слова) – 64-битная шина между ядром и кэшем второго уровня (Pentium III имеет 256 бит). Первые процессоры работали на шине 200 МГц (2х100), последующие модели перешли на 266 МГц (2х133). Набор команд x86, MMX, Enhanced 3DNow!</p>
<p>Июнь 2000 г. положил начало новому витку противостояния AMD и Intel, в этот раз – на рынке недорогих систем. Был выпущен процессор Duron 600, 650, 700 (позже 1.1 ГГц) на ядре Spitfire (переработанное ядро Thunderbird). Этот процессор содержит кэш-память обоих уровней на кристалле процессора L1 кэш – 128 Кб, L2 кэш – 64 Кб. Частота внешней шины 200 МГц (2х100), имеются 3 конвейера для операций с плавающей точкой.</p>
<p>2000 г. – на рынке появляется первый серийный процессор, от VIA получивший официальное название "C3". Технология производства 0,18 мкм. Количество транзисторов 11,2 млн. Разрядность 32 бит. Набор команд x86, MMX, 3DNow! Кэш-память первого уровня 128 Кбайт, кэш-память второго уровня отсутствует. Тактовая частота 500–667 МГц, частота шины 100–133 МГц. Разъём Socket 370. Выделяемая мощность 8–13 Вт. Преимущество процессоров C3 - малые потребляемая мощность и выделение тепла. Это позволяет использовать C3 без активного охлаждения (просто с радиатором).</p>
