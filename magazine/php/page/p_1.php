<H2>Теорема о представлении тензора поворота</H2>
<P>Согласно теореме Эйлера (34), любой поворот может быть осуществлен как один 
поворот вокруг некоторой оси на некоторый угол. Это удобно, когда ось 
фиксирована. Однако в большинстве задач это не так. Для эффективного решения 
любой задачи теории поворотов жизненно необходима следующая теорема.</P>
<P><B><I>Теорема.</I></B> <I>Любой поворот 
